    <div class="talk-description"><p>En général, les personnes qui sous-titrent ne connaissent pas les sourds, et de l'autre côté la technique est souvent méconnue du public sourd.</p>

<p>En quoi le sous-titrage sourd et malentendant est-il différent de la version originale sous-titrée, au delà des problèmes techniques et des référentiels d'accessibilité du sous-titrage sur le web, nous essayerons de voir les solutions les plus simples à mettre en œuvre.</p>

