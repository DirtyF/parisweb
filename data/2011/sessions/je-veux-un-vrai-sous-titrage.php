	<article id="transcription-246">
		<header><h2>Transcription</h2></header>
		<p>Je m'appelle Sophie, je vais vous parler de...<br />
Je suis sourde de naissance, je n'entends pas et c'est pas une chose qui est prête d'arriver.</p>

<p>Vous mettez des vidéos sur vos sites internet, et moi, ce que j'ai besoin c'est de les comprendre et c'est pour ça que je veux un vrai sous-titrage.</p>

<p>On va parler de couleurs, de en quoi consiste la qualité, comment faire pour avoir une bonne compréhension de la vidéo et enfin des bonnes pratiques de sous-titrage.</p>

<p>Le sous-titrage est apparu sur le web en 2002, grâce à Flash avec les plugins des players Flash qui sont arrivés en 2002. Ça a été la révolution avec l'arrivée du DivX. Ça a été la révolution du téléchargement des vidéos. Et pour enfin, pour terminer avec YouTube en 2008. Le 29 août 2008, YouTube a mis en place une interface de sous-titrage sur son site pour permettre à l'utilisateur de sous-titrer plus facilement les vidéos.</p>

<p>C'est l'explosion de la vidéo en ligne. Y'a plus de 2 millions de vidéos sous-titrées... Non ! Pas sous-titrées justement ! Plus de 2 millions de vidéos en ligne vues par jour, et combien il y a en de sous-titrées ? Une infime partie, quasiment rien.</p>

<p>La plupart du temps, on trouve des versions originales, qui est la traduction littérale de ce qui est dit.</p>

<p>Moi, ce que je voudrais, c'est du sous-titrage pour les sourds et malentendants en français. Le sous-titrage pour les sourds et malentendants, ça correspond à de la version originale avec des indications supplémentaires sur ce qui se passe dans la scène. Par exemple, y'a du bruit, y'a de la musique, y'a un téléphone qui sonne. C'est l'équivalent du closed-caption en anglais.</p>

<p>J'ai l'habitude de voir à la télévision les sous-titres en couleurs. C'est un automatisme, mon cerveau interprête le sous-titre qu'il voit à l'écran. Je sais par exemple avec ces couleurs-là... Le blanc qui est... Enfin, y'a deux groupes de couleurs, y'a le groupe de paroles et de bruits. Ici, on a le blanc, le jaune, le cyan, le vert qui sont... Enfin, ce qu'il y a marqué ici. Par exemple, quand c'est sous-titré en vert, je sais que c'est une personne qui va parler en langue étrangère et automatiquement mon cerveau traduit. Ça nous aide à comprendre plus facilement.</p>

<p>N'oubliez pas, rendre une vidéo accessible, ça n'est pas uniquement que le sous-titrage. Ce n'est pas que pour les sourds, c'est aussi la rendre accessible, c'est-à-dire, la rendre navigable au clavier, rajouter l'audiodescription pour les personnes aveugles. C'est pas si compliqué, il faut du temps, de la compréhension et de la volonté pour le faire. Y'a une présentation qui a été faite au W3Café, vous allez pouvoir trouver toutes les informations techniques et les différents players vidéo.</p>

<p>N'oubliez pas qui en bénéficie. Ces personnes, et vous ! Vous pouvez regarder des vidéos en open-space, ou dans les lieux publics, dans les gares, vous n'avez pas forcément le son sur la vidéo. À ce moment là, ça peut vous aider.</p>

<p>Il y a aussi une affaire de référencement, le sous-titrage est devenu une affaire de référencement. Sur YouTube, une vidéo est traduite automatiquement en 52 langues. Ce qui fait que notre vidéo est vue dans le monde entier et c'est encore meilleur en termes de référencement. Le sous-titrage, c'est comme les lunettes, un jour vous en aurez besoin.</p>

<p>[rires dans la salle]</p>

<p>Et enfin, les bonnes pratiques, je vous propose un travail que je fais en parallèle avec les associations de sourds et malentendants, les chaines de télévision et le CSA. J'en ai retenu les meilleures. </p>

<p>Ici, on a le respect au niveau du langage. <br />
C'est pas parce que... Par exemple, vous avez une vidéo où on montre une personne qui va dire "Yo ! Ça boome ?". Vous allez pas sous-titrer "Bonjour, vous désirez ?". Donc, si ça dit "Yo ! Ça boome ?", vous écrivez "Yo ! Ça boome ?". Respectez ce qui est dit. </p>

<p>Le respect des règles d'orthographe. <br />
C'est pas parce que on fait du bon français, qu'il ne faut pas faire du bon sous-titrage. </p>

<p>Le respect de l'image. <br />
Les 3/4 du temps, nous avons la vidéo avec la personne et les informations textuelles qui sont par exemple le nom de la personne avec sa fonction. Évitez, s'il vous plait, de mettre le sous-titrage à ce niveau là parce que parfois on a besoin de savoir qui est la personne qui parle et on sait pas parce qu'il y a le sous-titrage. D'autant plus que sur le web, on a la possibilité de sortir le sous-titrage de la vidéo, de le mettre en dehors de la vidéo. Donc, éventuellement, si on peut la sortir, c'est mieux. Si on peut pas, on la laisse sur la vidéo en la plaçant du mieux que possible et éventuellement avec un fond noir ou translucide. </p>

<p>Enfin, l'utilisation du tiret pour justement pouvoir faire la différence. Quand il y a une conversation, un dialogue, on sait qui parle. L'effet ping-pong, on sait jamais qui a dit quoi et quand il y a un tiret, on arrive à faire la différence de ces personnes-là. </p>

<p>L'utilisation du code couleur. <br />
Idéalement, ça serait bien d'utiliser le code couleur mais je comprends que c'est pas toujours possible. Une chose qui pourrait être, en remplacement du code couleur, c'est un équivalent pour le web. À défaut de mettre de la couleur dire par exemple quand il y a un téléphone qui sonne sur la vidéo, on va écrire "le téléphone sonne", et ça va être écrit en rouge. Si on n'a pas cette possibilité là, éventuellement la mettre entre crochets et dire "le téléphone sonne". Comme ça, on sait si c'est le téléphone qui sonne et non pas quelqu'un qui a dit que le téléphone sonnait.</p>

<p>Enfin, pour terminer, un bon découpage des phrases sur une vidéo.<br />
On peut avoir quelqu'un qui va dire "il aime les jeunes filles". Et ça va se dérouler sur plusieurs plans, sur le premier plan on va écrire "il aime les jeunes". Et sur le deuxième plan "filles". Donc idéalement, ça serait bien d'écrire "il aime les jeunes filles". Comme ça, ça prête pas à confusion et les gens se font pas d'idées sur ce qui va être dit. La qualité de votre sous-titrage, c'est ma compréhension.</p>

<p>Comment savoir qu'une vidéo est correctement sous-titrée ?<br />
Alors, première possibilité, prenez un casque anti-bruit, je ne sais pas si vous voyez... Vous entendez plus rien, vous entendez pas les gens autour de vous parler. Ou alors vous coupez le son de votre ordinateur et là vous voyez, et vous regardez le travail que vous avez fait sur votre vidéo. Si là vous comprenez ce qui a été dit, c'est que c'est un bon sous-titrage, sinon, ben, vous pouvez recommencer le boulot.</p>

