		<header><h2>Transcription</h2></header>
		<p>(Transcription par Tanguy Lohéac. Merci à lui)</p>

<p>Bonjour à tous. Je pensais avoir pris un sujet pas trop passionnant. <br />
Mais je vois que je me suis trompé un peu.</p>

<p>Donc Guillaume Abel. Je suis designer chez Ekino.</p>

<p>Je vais vous parler de l'influence du Bauhaus dans le design digital.</p>

<p>Pour commencer j'ai fait un petit historique de qu’est-ce que le Bauhaus.<br />
Le Bauhaus ce qu'il faut savoir c'est que ce n'est pas forcément un cours, pardon, pas un cours, mais un mouvement artistique.<br />
C'est avant tout une école avec le statut des arts et métiers qui a été créée en 1919 à Weimar dans un contexte un peu particulier puisque l'Allemagne venait de ressortir de la première guerre mondiale.<br />
L'Europe venait de ressortir de la première guerre mondiale.<br />
Et il y avait nécessité de former de nouvelles élites qui allaient distribuer ou faire de l'architecture.<br />
Elle a été fermée en 1933 à Berlin par le régime nazi.<br />
Bauhaus étymologiquement ça veut dire quoi ? Bau ça veut dire bâtiment en construction. Et haus pour maison. <br />
Et donc le Bauhaus : la maison du bâtir, la maison de la construction.</p>

<p>Donc, qu'est-ce qu'il y avait derrière ?</p>

<p>La volonté de mixer plusieurs courants artistiques et aussi de l'artisanat.<br />
De l’artisanat, comme je disais, de l'architecture, de la typographie, le design, le théâtre, peinture, sculpture, et donc de créer un lien entre ces deux mondes qui à l'époque étaient totalement hermétiques.<br />
Et donc casser un petit peu l'élitisme qu'il y avait côté artistique pour les tirer vers l'artisanat, et que l'artisanat aille plus vers le monde artistique.<br />
Là je vous ai juste mis les 3 bâtiments du Bauhaus. Ils ont donc commencé à Weimar. Ils ont déménagé ensuite à Dessau.<br />
Pour terminer à Berlin. Ce bâtiment est emblématique, puisque vous voyez que pour un bâtiment qui a été fait en 1924, il a des lignes très épurées et très modernes.<br />
Et c'est un petit peu justement les premiers fondamentaux qu'on va avoir sur le Bauhaus.<br />
Le Bauhaus ça a été aussi 3 directeurs qui ont apporté leur pierre à l'édifice, et aussi à chacun leur philosophie.<br />
Il y a eu donc le fondateur Walter Gropius dont un des éléments qui était dans le manifeste du Bauhaus c'était : le but de toute activité créatrice est l'architecture.<br />
Donc, cette pluridisciplinarité il y avait autour du Bauhaus avait comme but de pousser vers l'architecture.<br />
Un autre des directeurs était Hannes Meyer qui disait lui : « les besoins du peuple plutôt que les besoins du luxe. »<br />
C'est-à-dire que toutes les productions qui pouvaient sortir du Bauhaus devaient avoir un objectif d'être diffusées auprès du Peuple.<br />
Et enfin Ludwig Mies Van der Rohe lui, on va dire, c’est l’une des phrases qui a été le plus retenue dans le monde du desing, et souvent reprise c’est : « less is more » moins c’est plus. </p>

<p>Et donc il y a eu vraiment, comment dire, une émulsion au niveau de la créativité sur le design d’objets, de l’architecture.</p>

<p>Avec différents exemples ici.</p>

<p>Cette poterie par exemple, euh pardon pas poterie puisque du coup c'est une théière. <br />
Une théière en métal qui a eu beaucoup de succès, qui est un des premiers objets qui a été vendu par l'école et qui a eu énormément de succès.</p>

<p>On peut voir aussi un petit peu donc ce qui est ameublement. </p>

<p>D'autres exemples ici. Avec par exemple, moi j'ai eu des années cette chaise-là chez moi. <br />
Je savais pas du tout que c'était issu du design Bauhaus.<br />
Et après les caractéristiques du design de ces produits qui viennent des objets, c'est d'avoir un but d'industrialisation.<br />
À l'époque, c'est pour ça qu'il y avait ce besoin d'avoir des artisans, à l'époque les notions d'industrialisation et de production de masse étaient pas encore vraiment… ne faisaient pas partie des normes.</p>

<p>Et c'est là où ils ont inventé, on peut dire, le design industriel moderne.</p>

<p>Encore quelques exemples ici au niveau de l'architecture. </p>

<p>Donc là j'ai parlé justement à l'intérieur de l'école de Dessau.<br />
Des maisons de maître. Donc ils ont construit l'école, mais aussi autour, les maisons des enseignants qui étaient appelés des maîtres. J'vais pas parler de comment était organisée l'école. Ça va être un peu long sinon.<br />
Et on a aussi ici une autre façade du bâtiment Bauhaus que vous voyez. Une construction assez carrée, des lignes, des effets de, pardon, de grille.<br />
Et je vais prendre un dernier exemple de production de l'époque sur du travail sur le design graphique et sur la typographie.<br />
Et c'est vraiment ces points-là qui ont plutôt influencé sur le design digital.<br />
J'vais y revenir après.</p>

<p>Donc justement quelques exemples, enfin pas exemples, mais influences du Bauhaus.</p>

<p>"less is more", comme je le disais, c'est on va dire, qui a vraiment influencé beaucoup le design moderne, le design graphique moderne.</p>

<p>Et pour illustrer le propos « form follows function ».<br />
Excusez-moi hein.</p>

<p>Sur ce point-là c'est pas quelque chose qui est issu directement du Bauhaus.<br />
Mais c'était un courant dans l'architecture qui est venu se greffer aussi au Bauhaus.<br />
Et le Bauhaus on va dire a été l'école qui a beaucoup plus poussé cette notion comme quoi la fonction d'un objet et de n'importe quel type de construction doit suivre sa forme.<br />
Donc, à la limite c'est une illustration de vraiment différentes philosophies au niveau architecture.</p>

<p>On a le Seagram Building qui a été construit par le dernier directeur, donc Van der Rohe à New-York (ou Chicago ?).<br />
Chicago je crois.<br />
Et à côté la Sagrada Familia donc.<br />
Certes, c'est deux exemples où on a une architecture qui est plus du monde religieux et une autre qui est plus du monde fonctionnel, mais qui montrent que l'essentiel du Bauhaus se dégageait de tout ce qui est ornement, tout ce qui est accessoire dans par exemple l'architecture.<br />
Au niveau de l'influence, suite au démantèlement du Bauhaus en 1933, beaucoup de maîtres, beaucoup d'enseignants sont partis aux Etats-Unis et ont fondé différentes écoles.<br />
Comme par exemple le Black Mountain College qui a produit beaucoup de grands noms dans l'architecture. L'IIT Institut of Design donc c'est l'Illinois Institut of Technology and Design. <br />
De la même manière, il y a aussi un des enseignants qui est parti là-bas et qui a aussi monté toute une école d'architecture, mais aussi de design produit.</p>

<p>Et aussi des expériences sur la typographie.</p>

<p>Et enfin, dans la continuité du Bauhaus en Allemagne, l'Ecole Supérieur de Design d’Ulm.<br />
Et je vais illustrer justement l'héritage suite à cette école par Dieter Rams qui est issu de cette école, héritage du Bauhaus, et qui travaillait chez Braun.<br />
En quoi c'est important ?<br />
Il était designer produit chez Braun.<br />
Et ses produits ont beaucoup inspiré Apple, donc dans la construction.<br />
On a un poste radio, par exemple, sur la droite, et à gauche l'Ipod.<br />
Ou encore, je ne sais plus exactement ce que c’est, un poste radio et à côté, c'est quoi ? C'est un Mac Pro.<br />
Et enfin, un haut-parleur et à côté, l'Imac. <br />
Dans sa version la plus récente.</p>

<p>Donc, quels sont, on va dire, les différents éléments qu'on peut retenir du design Bauhaus ?</p>

<p>Symétrie : vous avez vu le bâtiment qui est construit assez carré.<br />
Angulaire : donc, pareil sur l'illustration.<br />
Même sur le travail du design graphique, vous avez vu qu'il y a beaucoup de travail sur l'angle.<br />
Ça peut être abstrait aussi. Les constructions typographiques que vous avez vues.<br />
Ou même certains objets.<br />
On ne comprend pas tout de suite exactement à quoi ça pourrait servir. <br />
Mais c'est une notion d'abstraction.<br />
Consistance : consistance ? ouais. <br />
Unité. c'est donc quelque chose qui sont assez uniformes.<br />
Organisation. Je vais aller un peu plus vite parce que bon...<br />
Economie, subtilité, continuité et régularité.</p>

<p>Ces éléments-là, dans les exemples que je vais vous montrer, j'essaie de les faire ressortir dans certains exemples de design digital.</p>

<p>Un premier exemple qui est Gmail. <br />
Ils ont fait une refonte il y a environ un an, en novembre.<br />
L'ancien Gmail ressemblait à ça. Donc déjà quelque chose on va dire, assez épuré.<br />
Mais ils ont encore réussi à rendre les choses encore plus légères et encore plus épurées, en faisant un travail à la fois sur l'espacement des tableaux, <br />
sur des boutons d'action qui sont très très clairement modélisés,<br />
un jeu de couleurs totalement différent,<br />
et des boutons qui ressemblent un peu moins à des boutons d'applications web, on va dire.<br />
Donc quelque chose d'un peu plus...<br />
mais ça c'est pour le web.<br />
Pour le web il y aura des tonnes et des tonnes d'exemples sur le web.</p>

<p>Ce que je veux prendre comme autres exemples c'est plutôt vraiment des applications mobiles où on a une notion d'interactivité un peu plus forte.</p>

<p>Donc par exemple Flipboard. J'sais pas si vous connaissez Flipboard.<br />
Mais c'est un agrégateur de flux RSS qui vous permet d'avoir une expérience de lecture différente.<br />
Donc là par exemple on a la homepage avec une image en plein écran,<br />
des typographies très très claires.<br />
Et ensuite, pour les différents éléments de vos flux RSS, ils sont découpés en différentes grilles.<br />
Et vous accédez à vos contenus en tapant dessus.<br />
Et après en tournant des pages. Comme si c'était vraiment un livre.<br />
Donc là j'ai réussi à placer un chat [rire salle].<br />
Et ce qui est intéressant, c'est que le contenu et l'article est dénué de tout ce qui est feuille de style CSS.<br />
Il est formaté pour avoir une expérience de lecture uniforme dans chaque article que vous allez regarder.</p>

<p>Après je vais prendre d'autres exemples en partant d'un type d'applications. </p>

<p>Donc gestionnaire de tâches. Donc voyez des gestionnaires de tâches.<br />
J'ai fait quelques screenshots pour montrer à quoi ressemblent généralement les gestionnaires de tâches sur l'Apple Store et sur l'Iphone.<br />
Donc, des choses à cocher,<br />
des cases à cocher. C'est un gestionnaire de tâches.<br />
Pour créer une nouvelle tâche, je passe par différents menus pour taper après le nom<br />
ou ce genre de choses.<br />
Et, pour les valider, j'appuie sur la coche.<br />
Et hop ! Elle disparaît.</p>

<p>Il y a une application qui s'appelle Clear qui a revu vraiment de manière ultra simplifiée comment je vais pouvoir créer des tâches et comment je vais pouvoir les gérer.<br />
Donc, là j'ai illustré les différents types d'interactions qu'on peut avoir.<br />
Pour créer, par exemple, vous avez une liste de tâches qui sont classées par ordre de couleurs.<br />
Donc, plus haut plus important.<br />
Et plus bas, moins important. <br />
Pour créer une nouvelle tâche vous glissez le doigt vers le bas.<br />
Donc, du coup, vous recréez un nouvel item.<br />
Pour pouvoir valider un item, vous glissez le doigt sur une tâche vers la droite. <br />
Si vous la supprimez, vous glissez sur la gauche.<br />
Vous voulez insérer ou ouvrir des groupes de listes, faut faire un pinch. <br />
Donc le pinch c'est serrer, pincer des doigts sur l'écran.<br />
Vous voulez insérer une liste entre deux tâches, vous faites l'inverse d'un pinch.<br />
C'est quoi déjà ? Pinch appart, très bien</p>

<p>Sur les applications météo :<br />
de la même manière j'ai pris l'exemple de l'application météo de base qui est sur l'Iphone, et une autre application météo qui s'appelle MeteoGroup, je crois.<br />
Et de la même manière, comment en enlevant tout ce qui est, on va dire, fioritures, on peut arriver à une application très très simple et qui justement projette, met en avant tout ce qui est interaction avec l'objet.<br />
Il y a Solar. Donc Solar c'est une application météo.<br />
Avec juste la température, normal, les dates, ce sont deux choses qui sont obligatoires, on peut pas passer à côté.<br />
Par contre ce qui est intéressant c'est qu'au lieu d'avoir des petites icônes qui nous montrent quel est le temps, ou il y a une modification de température ici, j'vous montre ici d'autres, ici vous allez avoir le temps qu'il va faire, la température ici.<br />
Et ce qui est intéressant c'est les interactions qu'il y a où sur 24 heures je peux glisser le doigt vers le haut.<br />
Et je vais avoir une évolution des couleurs du haut et des couleurs du bas, en dégradé,<br />
qui va me montrer : ben là il va faire nuit.<br />
Mais là je vois qu'il va faire froid.<br />
De la même manière, si je glisse vers le bas, autre interaction, je vais voir le temps à 3 jours +.<br />
Ou encore, si je glisse le doigt vers le côté, je vais voir les autres villes.<br />
Et je peux rajouter. Et de la même manière, j'ai un pinch pour pouvoir voir les différentes villes que je peux avoir.</p>

<p>Un autre exemple, un peu plus complexe.</p>

<p>Donc, à quoi ressemblent des samplers. Donc des logiciels qui permettent de faire de la musique avec des sons préenregistrés sur votre Iphone ou sur tablette d'ailleurs.<br />
Donc on voit, c'est quelque chose qui est pas simple.<br />
C'est des choses qui sont assez complexes où là je vais avoir les différents temps dans mon interface,<br />
des claviers qui peuvent être un peu complexes à utiliser aussi,<br />
des boutons qui permettent un petit peu de monter et de descendre les différentes phases d'attaque.<br />
Donc c'est des choses qui sont sur la musique, mais des effets sonores ou on reproduit en fait des éléments qui existent dans la vraie vie.<br />
Donc par exemple des tables de mixage. Ça ressemble beaucoup à ce genre d'éléments.<br />
Et c'est là où vraiment le Bauhaus dans le digital design peut apporter des choses assez intéressantes.<br />
Puisque là, ce qu'on appelle ces interfaces qui reprennent le réel c'est ce qu'on appelle les interfaces skew morphists.<br />
Et comment pour une interface de sampler on peut se dégager de ce skew morpism.<br />
Donc il y a un logiciel qui s'appelle Figure de Propellerhead qui est un peu connu dans le monde de la musique assistée par ordinateur.<br />
Et eux ils ont proposé quelque chose de très simple comme ça.<br />
Donc, je suis d'accord, une vidéo serait plus parlante, je vais essayer de vous expliquer le fonctionnement.<br />
Et en fait, quand par exemple je fais un play, je vais avoir ces petits cercles qui vont tourner.<br />
Et en fait ça c'est les différents temps.<br />
Donc par exemple là je sais que, je sais plus, huit temps, je vais avoir 4 fois 4 boums.<br />
Ça c'est un rythme, c'est un boum.<br />
Ça on va avoir par exemple du clap ou ce genre d choses.<br />
Et donc du coup, voilà l'interface totalement déroulée.<br />
On va avoir aussi un style graphique qui est très proche...<br />
J'sais pas si vous vous souvenez de ce que j'vous avais montré en termes de design graphique ?<br />
Au niveau code couleur, au niveau interface et au niveau design graphique, on a quelque chose qui est très très similaire et très fort par rapport au Bauhaus.<br />
Et là on a quelque chose qui peut paraître abstrait.<br />
Mais, encore une fois une vidéo est plus parlante. Mais lorsqu'on fait un play, on voit les éléments qui vont tourner.<br />
On va pouvoir jouer tout de suite et tout de suite s'approprier le logiciel.<br />
Et du coup on a quelque chose qui... less is more [sourire].<br />
C'est-à-dire qui est vraiment très très simple, qui prend toute sa logique lorsqu'on commence à l'utiliser.</p>

<p>Un autre exemple, qui à mon avis, et j'l'ai mis en dernier et je pense que certains y ont déjà pensé,</p>

<p>C'est Windows 8.</p>

<p>Là on assiste en ce moment, pour la prochaine sortie de Windows,<br />
Ils ont carrément tout modifié par rapport au paradigme du bureau, de l'icône, euh pardon, oui de l'icône, du pointeur de souris.<br />
Et par exemple sur tous les bureaux on a une corbeille.<br />
Ils se sont totalement... [visiblement rappelé à l'ordre sur le temps dépassé]<br />
Sans déconner ?<br />
Déjà la fin ?! [sourires dans la salle]<br />
Noooon !<br />
OK. Bah Windows 8 c'est bien ! Voilà ! [rires de la salle + applaudissements]<br />
Allez-y !<br />
J'ai pas du tout vu.<br />
Et puis euh, la conclusion euh, soit vous venez m'voir, soit j'ai pas de question.<br />
C'est très bien, c'est pas grave.<br />
2, 3 mots non ?<br />
Non. Désolé hein. J'suis vraiment désolé.<br />
Juste rapidement. Ce qui est intéressant dans l'héritage qui vient du design Bauhaus c'est la pluridisciplinarité.<br />
On a vu qu'on a rassemblé des gens qui étaient d'horizons différents pour travailler ensemble sur un côté artistique de l'architecture.<br />
Et dans le design digital on voit la même chose : des codeurs qui vont travailler avec des graphistes.<br />
Et bon, de l'artisanat à l'industrie, le web c'est un peu de l'artisanat et encore un petit peu.<br />
Même si on glisse vraiment de plus en plus vers de l'industrialisation.<br />
Et enfin, on est toujours tourné vers l'humanisme.<br />
Donc les valeurs du Bauhaus étaient très humanistes.<br />
À l'époque c'qui a pas fait le... on va dire c'était pas très cool vis-à-vis des nazis.<br />
Et du coup, maintenant on reprend un peu ces valeurs de mettre au centre de la réflexion de nos produits digital les utilisateurs.</p>

<p>Voilà ! J'ai dépassé de combien ?<br />
OK [rire].<br />
[applaudissements]<br />
Merci ! </p>
