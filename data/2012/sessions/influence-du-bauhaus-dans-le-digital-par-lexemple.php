    <div class="talk-description"><p>Né en Allemagne en 1923, le Bauhaus est un courant qui toucha, l’architecture et le design, mais également la photographie, le costume et la danse.</p>

<p>L’un des objectifs de ce courant était d’unifier l’art, l’artisanat et la technologie. L’usage de la machine est considéré comme positive. Le design industriel et produit en sont donc des composantes importantes.</p>

<p>Après avoir inspiré certain succès en terme de design produit (Apple), beaucoup notent une tendance de fond dans les expériences digitales qui reflètent plusieurs principes et valeurs du design Bauhaus.</p>

<p>Dans cette intervention, il sera peu question de théorie sur le design Bauhaus, mais plutôt d'illustrer son influence par une série d’exemples de sites web, d'applications mobiles et tablettes.</p></div>
