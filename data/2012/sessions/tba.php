		<header><h2>Transcription</h2></header>
		<p>Je m’appelle Régis. C'est moi. [Sourire]<br />
La classe !<br />
Donc je travaille dans une agence…<br />
Non, vous savez quoi ? Je vais pas parler de ça.<br />
Plutôt que de vous parler de ce que je fais, je vais vous parler de qui je suis exactement.<br />
Donc je suis le genre de personne qui peut se jeter corps et âme dans une aventure, simplement parce qu’il y croit alors que tout le monde me dit que c’est pas le cas.<br />
J’ai fait beaucoup de mal. J’ai fait beaucoup de bien aussi.<br />
Cette main est capable d’écrire des choses qui peuvent convaincre une personne qu’elle est le centre de l’univers.<br />
Qu’est-ce que je peux dire d'autre sur moi ?<br />
J’ai fait des choses super bien ! J’ai changé le monde !<br />
A mon échelle, mais j’ai changé le monde quand même.<br />
J’ai souffert, j’ai jouis,  plein de choses.<br />
Rien que je regrette.<br />
A vrai dire, si je suis comme ça, si je suis là devant vous aujourd’hui, c’est grâce à toutes ces choses.<br />
 Donc j’en suis vraiment très fier.<br />
Ça c’est moi encore torse nu, juste pour vous dire qu’à partir de huit ans j’ai eu mon premier T-shirt. [Rires]<br />
Et ça c’est moi il y a quelques mois parce que je me suis vraiment demandé ce que j’allais vous dire pendant ces 50 minutes-là.<br />
J’ai commencé vraiment à flipper.<br />
Heureusement, j’ai eu la bonne idée d’aller sur le site de Paris Web.<br />
Et c’est là que j’ai vu en fait que l’équipe de Paris Web avait eu la bonne idée de mettre un thème à ma présentation.<br />
Et ça je pense que je peux vraiment le faire.<br />
Donc c’est parti !<br />
Alors autour de cette présentation, je vais vous parler de la fin du monde, de Satan et bien entendu, du New York Times.<br />
On va commencer par la fin du monde.<br />
Est-ce que quelqu’un sait ce que ça veut dire 13.0.0.0.0 ?<br />
Non ?<br />
C’est une date dans le calendrier maya.<br />
En fait c’est pas vraiment une date.<br />
Le calendrier maya c’est plutôt des durées.<br />
Il faut savoir qu’ils ont un système de datation assez particulier.<br />
Moi je le trouve très sexy en fait.<br />
C’est assez facile de calculer. À gauche vous avez une date dans le calendrier maya, à droite, le nombre de jours.<br />
Et en fait ce nombre, c’est une période de plus ou moins presque 2 millions de jours.<br />
Et ça correspond en fait dans leur mythologie à la durée du monde.<br />
Donc c’est pour ça que depuis quelque temps on parle de la fin du monde.<br />
Mais il faut savoir que pour les Mayas, ça ne fonctionne pas exactement de manière séquentielle comme ça.<br />
C’est plutôt des cycles.<br />
C’est-à-dire qu’à la fin du monde il y a un nouveau monde qui arrive.<br />
C’est ça qui est vraiment chouette !<br />
C’est assez rassurant.<br />
Parce que vous pouvez être sûr que le 21 décembre il ne va rien se passer de bizarre. Et par contre, ils vont faire la fête ça c’est clair !<br />
Il y a des chances que beaucoup de gens dansent tout nus dans la rue.<br />
Imaginez que vous fassiez le Nouvel An une fois tous les 2 millions de jours. Vous pouvez vous représenter ce que ça représente pour eux. [Rire]<br />
Par contre, ce cycle en fait correspond à la création de l’homme.<br />
C’est-à-dire qu’à chaque fois, il y a eu une tentative de création de l’homme à partir d’une matière.<br />
Il y en a plusieurs qui ont échoué, une qui a fonctionné.<br />
Et je sais pas si vous êtes au courant, mais on est sur le point de vivre, encore heureux, mais on est en train de vivre une véritable transition en tant qu’êtres humains.<br />
Il y a quelques mois je suis tombé sur cette présentation qui parlait de l’homo evolutis en fait,<br />
et dans laquelle ce monsieur explique simplement qu’on est désormais capable de maîtriser notre propre évolution.<br />
C’est-à-dire qu’on a décodé l’ADN, on sait faire des transplantations, on sait faire des greffes, on sait faire du clonage.<br />
Donc il n’y a rien qui nous limite aujourd’hui, techniquement parlant, à faire des choses qui nous permettent de devenir plus que des êtres humains.<br />
Alors ça n’a absolument rien à voir avec mon speech.<br />
Mais par contre, j’ai cette impression que sur le Web, on est arrivé plus ou moins à cette même phase.<br />
C’est-à-dire qu’on a toujours été considéré un petit peu comme les enfants terribles de l’imprimerie, du marketing, de la communication, du logiciel,<br />
et on n'a jamais vraiment eu une histoire qui nous était propre.<br />
Or, depuis quelques temps, bon, vous savez qu’il y a eu une période où les standards ont vraiment été sous le feu de la rampe.<br />
Mais aujourd’hui on vit une transition où on commence à avoir vraiment notre corps de littérature, notre corps de savoir, qui n’a presque plus rien à voir en fait avec la littérature des autres disciplines.<br />
Je pense qu'en fait, c’est vraiment l’opportunité pour nous de voler de nos propres ailes et de montrer vraiment ce que l’on peut créer de particulier sur Internet.<br />
Alors un petit peu de background.<br />
Il faut savoir que j’aime beaucoup lire.<br />
Je pense que c’était mis dans ma bio.<br />
J’aime beaucoup lire simplement parce que j’aime le contact du papier dans mes mains. <br />
Mais aussi parce qu’une partie de moi, je la déverse à chaque fois dans le livre que je lis, en particulier celui-là.<br />
C’est un livre qui m’a vraiment marqué. Je sais pas si quelqu’un connaît dans l’assemblée ?<br />
Excellent !<br />
Il faut savoir que j’ai mis un an à le lire parce que c’était vraiment très dense.<br />
J’ai appris beaucoup de choses.<br />
Mais le truc c’était que le Régis avant et le Régis après, c’était pas du tout la même personne.<br />
Je peux même dire que je voyais la vie d’une manière différente, que ça a complètement changé ma vision des choses et la vision que je me faisais de ma propre vie.<br />
Et à vrai dire, en fait, quand je  tiens ce livre en main, j’ai des tas de souvenirs qui me reviennent en tête, et des tas de choses en fait que je revis et qui me font ressentir des choses vraiment extraordinaires.<br />
Oui un livre peut faire sentir des choses extraordinaires.<br />
Et en fait, il y a quelque temps, il y a eu cet article qui est paru sur le magazine A List Apart qui parlait plus ou moins de la même chose : de notre capacité à pouvoir décharger des émotions et des souvenirs dans les objets.<br />
Je pense que c’est quelque chose qu’on fait sans cesse. <br />
Sans quoi, on serait en fait incapable de vraiment se concentrer sur des tâches.<br />
C’est-à-dire par exemple, vous avez les photos de vacances. <br />
Ces photos de vacances, elles vous servent dix ans plus tard à retrouver les sensations que vous aviez lorsque vous étiez en vacances.<br />
Mais entre-temps, vous avez complètement oublié que ces sensations existaient.<br />
C’est la même chose avec plein d'objets.<br />
Mais j’ai aussi l’impression qu’on ne se focalise pas uniquement sur des objets.<br />
Il arrive souvent, je pense que tout le monde a fait cette expérience, de regarder des gens dans la rue, heureux, de voir des amoureux s’embrasser, des gens se tenir la main et de se dire…<br />
Enfin à ce moment-là, de sentir quelque chose de plus que : "oui ça me fait plaisir de voir ces gens-là !"<br />
Mais avoir vraiment une réaction chimique qui se passe en soi.<br />
On se sent un peu plus léger, on a l’impression que la journée va être extraordinaire.<br />
On se sent pousser des ailes en fait.<br />
Ma thèse aujourd’hui c’est de vous montrer qu’on est capable de faire exactement la même chose avec des sites Internet.<br />
C’est-à-dire que je pense qu’on peut faire ressentir des choses, qu’on peut créer des liens émotionnels vraiment avec une page Web.<br />
A vrai dire, il y a un fondement scientifique à ça.<br />
C’est ce qu’on appelle la contagion émotionnelle.<br />
Donc, quand on voit des gens par exemple, on déverse sur eux des sensations que nous-mêmes on a ressenties auparavant, mais qu’on revit à travers leur propre expérience.<br />
Et c’est pareil sur une page Web à vrai dire.<br />
La façon dont on communique, la façon dont on véhicule l’information, elle peut créer chez nous, déclencher des mécanismes qui nous rappellent, qui nous évoquent en fait des sensations.<br />
Alors je dis pas qu’on est capable de faire l’ingénierie des sentiments.<br />
A vrai dire je pense que c’est plutôt un mécanisme qui naît de tellement de paramètres qu’on peut pas vraiment le prédire.<br />
J’aime bien penser aussi que c’est quelque chose d’organique.<br />
C’est-à-dire que quand on conçoit un site, j’aime bien que ce soit une espèce d’échange avec l’internaute.<br />
C’est-à-dire, on lance quelque chose en ligne, on voit un petit peu comment ça marche, et on adapte au fur et à mesure en fonction des réactions.<br />
On mesure on adapte, on mesure on adapte.<br />
Est-ce que c’est important aujourd’hui ?<br />
Je pense en effet.<br />
A vrai dire, la présence en ligne de certains sites est tellement bonne au niveau technologie et au niveau design qu’aujourd’hui les gens se sont plus ou moins habitués à un niveau de qualité qui oblige en fait tout le monde à s’aligner là-dessus.<br />
Et petit à petit, on remarque de moins en moins, en tout cas on fait de moins en moins attention à ce niveau de qualité.<br />
Je crois qu’on a besoin en fait d’aller un petit peu plus loin dans la création des expériences.<br />
Et en l’occurrence, moi je pense que l’une des choses à laquelle on peut s’intéresser, c’est vraiment de toucher les gens au plus profond d’eux-mêmes, d’essayer de créer chez eux quelque chose de mémorable qui va les faire réagir, qui va vraiment les intéresser.<br />
Être honnête, avoir de l’humilité, se présenter en tant qu’être humain, essayer de partager avec eux des expériences, montrer nos faiblesses, montrer nos forces.<br />
Je crois que c’est quelque chose qu’on peut vraiment explorer pour créer des expériences qui ont vraiment du sens.<br />
Donc là je vais vous passer en revue plusieurs expériences que j’ai vécues moi-même.<br />
Donc je vous présente Christine.<br />
Elle est belle hein ? [Rire]<br />
C’est la femme de mon collègue Christophe. [Rires et applaudissements]<br />
Le mariage. Donc les yeux qui pétillent, l’amour, des superbes souvenirs, des moments vraiment extraordinaires.<br />
Et on peut regarder ça sans cesse, on retrouve encore ces sensations.<br />
Je sais pas si vous connaissez ce site ?<br />
Ça a été créé par un designer assez célèbre en fait qui a créé le site de Nike, vous savez qui scrollait de façon tout à fait bizarre.<br />
C’est une espèce de journal intime.<br />
Et pourquoi je vous parle de ça ?<br />
C’est parce que le mariage c’est clairement quelque chose, en tout cas quand ça se passe bien, que, en tant que personne si on a un journal intime, c’est le genre de choses qu’on envie de noter, qu’on a envie de consigner, qu’on a envie de garder avec soi.<br />
Et en fait, en parcourant ce site Internet-là, on a l’impression vraiment de s’immiscer dans la vie personnelle du gars et de parcourir son journal intime à lui en fait.<br />
D’ailleurs il est vraiment couillu de publier ça en ligne.<br />
Parce que je trouve que c’est tout à fait personnel, c’est une grande preuve d’ouverture de mettre ça à la vue de tous.<br />
Donc allez-y, ! Je vous encourage vraiment à aller sur ce site.<br />
A vrai dire, s’il vous fait pas vibrer je pense que vous n’avez aucune émotion. Donc voilà.<br />
Pourquoi est-ce que je pense que ce site est extraordinaire ?<br />
Je crois en fait que c’est la première fois que je tombe sur une page qui me fait complètement oublier que je suis sur Internet à vrai dire.<br />
J’ai plus l’impression même de parcourir un livre physique que de parcourir une page Web.<br />
Je crois qu’en grande partie c’est dû à ce mariage, c’est vraiment cette symbiose totale entre la façon dont le contenu est mis en page, le texte évidemment qui est vraiment formidable, et les interactions.<br />
Les interactions c’est époustouflant, parce que tout est calculé, tout est millimétré :<br />
la cadence du contenu, la narration, la taille des images, le temps d’une transition entre un article et un autre, le temps d’une transition entre une page et une autre, tout a été vraiment calculé pour donner en fait cette impression qu’on est dans un espace physique.<br />
Et je trouve ça vraiment unique. Alors juste un petit mot sur le point de vue technique. Je vais pas entrer dans les détails mais…<br />
Il est vrai que c’est vraiment ennuyant de scroller une page Web, vous trouvez pas ?<br />
Ça va de haut en bas, ya jamais rien qui se passe.<br />
Et depuis quelques temps, enfin même depuis plusieurs années, il y a beaucoup de designers qui essaient de sortir de ce carcan et de créer quelque chose en plus, d’ajouter une touche de leur personnalité ou de créer une histoire en fait, qui met le contenu en valeur ou qui le glorifie de telle façon qu’on le comprend à un autre niveau.<br />
Vous avez certainement déjà vu ce site par exemple…<br />
C’est pour une application iPhone.<br />
Qui s’appelle Ben the bodyguard<br />
et vous voyez le type en l’occurrence qui marche, au fur et à mesure que vous scrollez, vous voyez le personnage qui marche dans la rue et qui vous parle, qui interagit avec vous.<br />
Et ça représente… Ça m’a fait revivre moi en tout cas personnellement les jeux vidéo des années 80 sur ma Méga Drive.<br />
C’était vraiment chouette ! Parce qu’on avait l’impression, moi j’avais l’impression d’être le personnage au centre de l’action et c’est…<br />
En l’occurrence, j’étais hyper impliqué dans la lecture de la page.<br />
Un autre exemple encore :<br />
vous avez certainement vu ce site qui fait la part belle à Internet Explorer 9 en montrant des exemples d’utilisation des Webfonts.<br />
