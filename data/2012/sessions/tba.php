il se trouve que moi aussi je travaille dans un journal, mais en tant que développeur.<br />
Du coup je m’intéresse pas mal à ce que les gens disent, etc., sur le site.<br />
Et notamment il y a une réflexion de ma mère qui m’avait dit que sur le site sur lequel je travaille, Libération pour pas le citer, que par rapport à tous les autres en fait,<br />
la home du site était extrêmement simple.<br />
C’est juste un flux tout bête, tout moche.<br />
Et ce qui est marrant, c’est qu’à l’intérieur de Libération…<br />
Tout le monde le déteste en fait ce site.<br />
Tout le monde voudrait un truc super design, dans le genre de USA Today ou autres, qui flashe de partout, etc. Et de l'autre côté, je pense que il y a un certain nombre de publics qui veulent s’informer et pour lesquels, avoir un flux tout bête, hyper neutre, hyper simple, finalement, c’est mieux pour arriver à véhiculer l’information.<br />
Voilà. C’est pas vraiment une question c’est juste…<br />
Régis : non mais je suis totalement d’accord, il y a deux approches.<br />
Je sais pas si… J’ai jamais été voir sur le site de Libération mais…<br />
Vous avez certainement vu, l’agence Mule a récemment mis en ligne un site qui s’appelle la Gazette… Peu importe… La Gazette du soir.<br />
Et c’est ça… C’est vraiment une page, une page avec une sélection des meilleurs articles de la journée. Et ça se limite à ça.<br />
Paf ! On clique sur un lien, on va sur le site en question.<br />
Oui, il y a des gens qui sont attirés par ce genre d’expérience, il y en a d’autres qui ont envie d’être entertained ? Je sais pas comment dire en français.<br />
Quelqu’un dans la salle : diverti.<br />
Régis : diverti. Je suis belge, hein ! [Rires]<br />
Oui. Tu as raison. Absolument. Mais par contre, là où je me pose la question :<br />
toi en tant que développeur, est-ce que tu ne regardes pas les choses d’une manière différente qu’une personne lambda qui, elle, serait peut-être surprise par la présentation, et serait justement embarquée dedans.<br />
Tu vois ? Elle serait comme embarquée dans une histoire.<br />
Et maintenant, ta maman fait peut-être partie des gens qui ont juste besoin de l’information brute, et pas de distraction.<br />
Tandis que d’autres à l’opposé peut-être ont besoin de décorum en fait pour lire un article, ou simplement pour s’intéresser à la presse.<br />
Ils ont besoin de quelque chose d’un peu plus que le contenu pur et dur.<br />
Je sais pas si ça répond ?<br />
IS : si si ça réponse.<br />
Régis : merci.<br />
Président de session : on a juste une dernière question.<br />
Question n° 5<br />
IS : je voudrais revenir sur le site du designer dont tu parlais, qui exposait beaucoup sa vie, qui travaillait sur un journal intime. J’étais un peu surpris dans le sens où tu dis : je trouve ça étonnant quelqu’un qui s’expose autant sur le Web.<br />
Alors je pense qu’on a tous des gens qui sont sur Facebook et qui s’exposent tant que ça.<br />
Régis : ouais.<br />
IS : du coup je me posais la question : est-ce qu’au cours de tes recherches, justement, tu as vu un petit peu une sorte de changement de mentalité sur l’exposition des émotions, être très transparents sur Internet, et un peu, pas s’exhiber, mais montrer ce qu’on ressent ou partager ce genre de sentiment ?<br />
Régis : alors je sais pas s’il y a vraiment eu un shift. <br />
Mais c’est plutôt de façon ponctuelle en fait. De temps en temps on tombe sur un article dans un blog où par exemple un designer commence à parler de ses enfants.<br />
Et simplement le fait de basculer de sujets ou simplement de pas parler de design, ça change complètement la donne.<br />
Maintenant c’est vrai que je remarque ça de plus en plus.<br />
C’est-à-dire qu’il y a certains designers qui commencent simplement à montrer leur côté humain plutôt que leur qualité technique.<br />
Ils ont tendance à s’épancher un peu plus. C’est peut-être pas seulement le cas des designers ?<br />
C’est parce que moi, c’est le sujet qui m’intéresse.<br />
Et en l’occurrence je pense pas vraiment qu’il y a eu un changement de mentalité comme ça brut. ça s’est fait progressivement. C’est simplement le fait de remplacer un support physique comme un journal intime par exemple, par le papier… par un site Internet.<br />
Mais par contre, le site en question c’était vraiment le premier que j’ai rencontré où il y avait vraiment que ça, en fait.<br />
C’était vraiment…<br />
Pour quelqu’un qui travaille dans le monde du design, je veux dire qu'il y a énormément de blogs qui sont des journaux intimes en ligne. Mais là, c’est vraiment quelqu’un qui parle que de sa vie, de ses expériences, de ses voyages, de ses échecs, de ses réussites, de choses comme ça.<br />
[Applaudissements]</p>
