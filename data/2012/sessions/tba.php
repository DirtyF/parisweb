    <div class="talk-description"><p>Nous sommes des créateurs privilégiés. Pendant des années, des femmes et des hommes se sont battus pour le web que nous avons aujourd’hui. Ils ont évangélisé une religion sacrilège, la standardisation. Ils ont joué aux savants fous en fusionnant plusieurs sciences en une seule, l’expérience utilisateur. Ils ont défendu les intérêts de personnes qui ne soupçonnent même pas leur existence. Et ils ont tissé les fils d’un futur où nous pourrons à notre tour en faire de même, moins les contraintes.</p>

<p>Ce futur est enfin arrivé. Nous avons les outils. Nous avons les supports. Les techniques. La science. L’art. La question que tout le monde est en train de se poser en ce moment, c’est : qu’en faire ? L’intelligentia du web réalise enfin que ce qui compte, ce ne sont ni les techniques dernier cri, ni la maestria des artistes digitaux ; ce sont les interactions qui naissent de leur combinaison, ce sont les expériences mémorables qui n’auraient pas été possibles sans elles.<br />
Voyons cela en détail avant que l’univers n’implose.</p></div>
