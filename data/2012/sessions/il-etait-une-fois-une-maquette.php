    <div class="talk-description"><p>Un intégrateur HTML pleure de joie.<br />
C'est un grand jour pour sa maquette.<br />
Elle qui avait été conçue de son union avec un graphiste.<br />
Elle qui était née d'un besoin exprimé par un client.<br />
Elle qui avait grandi jusqu'à être compatible sur de nombreux navigateurs.<br />
C'est le jour J : elle va enfin rencontrer celui pour lequel elle était promise.<br />
Elle va s'unir avec un code métier.</p>

<p>Sa vie ne sera plus jamais la même : on va désormais l'appeler thème (pour Wordpress), ou template (pour Symfony). Elle ne pourra plus jamais être lue dans un navigateur sans son code métier. L'intégrateur n'aura alors plus la main directe sur sa maquette…</p>

<p>Ceci arrive tous les jours, et ce n'est pourtant pas un événement anodin :</p>

<ul>
<li>Quels sont les réels impacts de cet événement sur la vie de votre projet ?</li>
<li>Est-ce qu'il est possible de travailler par itération avec ce fonctionnement-là ?</li>
<li>Ne faudrait-il pas laisser une maquette vivre sa vie, même après son union avec du code métier ?</li>
<li>Comment concilier alors la vision modulaire du code métier et la vision hiérarchique de l'HTML ?</li>
<li>Quels sont les alternatives pour mieux respecter le travail de chacun ?</li>
</ul>

<p>Ces questions, que je me suis posées, méritent de plus amples réflexions. Sans vous donner de solutions universelles, j'essaierai de vous donner des pistes de réflexion sur la collaboration entre intégrateurs HTML et développeurs back.</p></div>
	</section>

	
	






