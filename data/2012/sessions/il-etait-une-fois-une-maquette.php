		<p>(Merci à Tanguy Lohéac pour la transcription.)</p>

<p>Thomas Zilliox : bonjour à toutes, bonjour à tous.<br />
Merci d’être là.<br />
Alors je vais vous présenter une conf qui s’appelle : il était une fois une maquette.<br />
Alors on va regarder si… Hop.<br />
Il était une fois une maquette. Et si on veut y mettre un peu de troll dedans, j’ai envie de dire intégrateurs HTML et développeurs métier, est-ce qu’on pourrait commencer à collaborer efficacement ?<br />
Alors moi je me présente. Je m’appelle thomas Zilliox. Je fais du web depuis un peu plus de 5 ans maintenant.<br />
J’suis démarré en freelance il y a pas longtemps en tant que Pixel 418.<br />
Et je fais aussi bien de l’intégration que du développement.<br />
Et sur des projets je fais de l’intégration, sur des projets je fais du développement.<br />
Et assez rarement, je fais l’intégration et le développement.<br />
Parce que quand je fais les deux, il se trouve que après je m’énerve, je me boude.<br />
Et pourtant c’est pas un problème de communication parce que d’habitude je me communique assez bien avec moi.<br />
Et finalement, j’ai essayé de chercher le pourquoi du comment.<br />
Et c’est ces pistes de réflexion que je vais vous présenter aujourd’hui.<br />
Donc c’était l’histoire d’une maquette.<br />
Donc la maquette elle est faite par l’intégrateur HTML, qui avait été faite par les graphistes avant, qui venait d’un besoin client.<br />
Et d’un autre côté le client il va définir un périmètre fonctionnel.<br />
Il y a un chef de projet qui va les transformer en questions.<br />
Et le développeur va faire le code métier.<br />
Alors développeur métier c’est une traduction personnelle de développeur back.<br />
Et finalement la maquette et le métier sont à l’opposé mais pourtant ils sont faits l’un pour l’autre.<br />
Le métier est fait pour la maquette, la maquette est fait pour le métier.<br />
Et ils vont se marier.<br />
Mais c’est le développeur qui va les marier ensemble.<br />
Jusqu’au jour où le client trouve un bug.<br />
Il le dit au chef de projet qui le renvoie au développeur.<br />
Et le développeur dit : « ah ça c’est un bug IE6, c’est pas pour moi ! Je leur envoie l’intégrateur. »<br />
Et l’intégrateur : en effet c’est un bug IE6.<br />
Il se trouve un peu embêté parce qu’il sait bien que sa maquette… bon il sait pas trop ce qui se passe de l’autre côté.<br />
Mais il sait que sa maquette elle est plus comme avant. Elle a changé.<br />
Donc il va chercher à reprendre du site en ligne le code HTML pour le modifier, faire des corrections et le redonner au développeur pour le réintégrer.<br />
Et là l’intégrateur dit une phrase qui reste dans la mémoire du développeur. Quelque chose comme ça :<br />
« Tiens j’ai corrigé le bug IE6. Et j’en ai profité pour aligner le footer, corriger le rang de recherche, améliorer le contraste des liens sur le côté et réaligner le header. »<br />
Et là le développeur s’est mis à pleurer en fait parce que il a que deux choix. <br />
L’un c’est refaire son boulot. <br />
Et le deuxième c’est comparer à l’œil nu les différents fichiers, essayer de trouver les différences, copier/coller.<br />
Ce qui fait que dans les deux cas ça va être long et que dans les deux cas, il va se retrouver avec des nouveaux bugs.<br />
Et c’est pour ça que c’est l’histoire d’une maquette.<br />
C’est-à-dire que la maquette dans vos projets, la plupart du temps c’est du one shot.<br />
La maquette est très jolie. Elle est insérée, enfin elle est donnée au développeur et après on l’oublie.<br />
Ca devient une template.<br />
Pour la gestion de projet, c’est pas pratique.<br />
Donc on va essayer aujourd’hui de définir un coupable.<br />
Et le coupable bein il y a en déjà 5 des suspects.<br />
Donc à vous de choisir lequel vous paraît le plus coupable.<br />
Sachant que la bonne poire c’est souvent le client [quelques sourires dans la salle].<br />
Alors on va commencer l’enquête. <br />
Quand on va faire notre enquête de terrain on va tout de suite voir qu’il y a deux visions complètement différentes.<br />
Celui de l’intégrateur et celui du développeur.<br />
C’est-à-dire que quand on va commencer à poser des questions par rapport au client., il y en a un qui va nous parler d’un contact au marketing et l’autre qui va nous parler d’un contact au service informatique.<br />
Il y en a un qui va parler d’un site web, l’autre d’une application web.<br />
L’autre qui nous parle que de fonctionnel, l’autre que de l’apparence. <br />
Et finalement le résultat de leur travail, c’est pas du tout le même.<br />
Il y en a un qui va nous faire une maquette et il y en a un qui va nous faire une template.<br />
Et la différence est énorme. C’est-à-dire que si je fais un zoom sur deux caractéristiques essentielles, enfin deux différences essentielles entre la maquette et la template.<br />
C’est-à-dire que, pour résumer, la maquette elle va être en HTML et CSS.<br />
Et par nature ces langages sont hiérarchiques et globaux. Enfin ils ont une vision hiérarchique et globale.<br />
C’est-à-dire que quand vous modifiez une CSS elle risque d’affecter toute la page, voire même toutes les pages.<br />
Donc vous êtes toujours obligé de réfléchir globalement.<br />
Et quand vous faites de l’HTML, votre nœud HTML il a de sens que par sa hiérarchie.<br />
On le déconnecte pas du reste.<br />
On pense toujours globalement et hiérarchie.<br />
Alors que les développeurs web, back, ils travaillent pas du tout comme ça.<br />
Ils ont leur métier, besoins métier :<br />
Je prends un élément de la colonne de droite, je la fous à gauche…<br />
Si on peut le faire, pourquoi pas le faire.<br />
Et les framework c’est pareil. On découpe les problèmes.<br />
Moins on a d’interaction entre les éléments de notre projet, mieux c’est: Plus on va pouvoir travailler à plusieurs, plus on va pouvoir découper notre code, corriger un bug d’un côté sans casser le reste.<br />
Et finalement c’est deux visions qui sont complètement différentes.<br />
En fait on donne la maquette qui est une vision hiérarchique et globale au développeur, qui a lui, une vision de son projet inversement.<br />
Et ce développeur on lui donne des outils qui sont des outils de développeur.<br />
C’est-à-dire qui sont découpés et modulaires.<br />
Donc il va nous faire un truc comme ça. Pour afficher une liste de liens de produits :<br />
Il va nous coller un ul.<br />
Il va nous faire un foreach avec des accolades dedans.<br />
Et puis un include.<br />
Donc là on a deux gros problèmes. C’est-à-dire que première accolades pour mettre un foreach, ça veut dire que notre maquette on peut plus la voir dans un navigateur.<br />
C’est fini.<br />
Il y a des include.<br />
Donc bein là on a un ul, on s’attendrait à avoir un li.<br />
Et on un include de fichier.<br />
Donc est-ce qu’il y a un li de fichier ? est-ce qu’il va rester longtemps ce li ?<br />
Quand une première personne va essayer de maintenir, il va peut-être l’enlever.<br />
Donc c’est pas du tout ce qu’aurait fait un intégrateur.<br />
On va essayer de chercher donc la solution idéale.<br />
Est-ce qu’on peut changer les outils ?<br />
C’est-à-dire que au développeur on lui donne un ciseau. Donc on va pas s’étonner qu’il découpe le papier avec quoi.<br />
Donc à la recherche de la solution idéale, on va plutôt se pencher sur ce qu’il y a côté JavaScript.<br />
Parce que JavaScript c’est une technologie qui a un peu ce côté de vision d’HTML.<br />
Donc je vous prends ces deux projets que vous connaissez peut-être déjà qui s’appellent BarmanJS et Distal qui sont des outils de templating en JavaScript,<br />
Et qui en fait travaillent pour mettre les informations de templating, toute la logique du templating, en attributs HTML5.<br />
Donc votre template HTML elle est toujours valide dans un navigateur.<br />
Votre maquette vous pouvez, enfin votre template du coup, vous pouvez la donner à votre intégrateur.<br />
Il peut la modifier. Et vous ça vous gêne pas.<br />
Il va pouvoir la modifier dans son navigateur.<br />
Mais ça va pas assez loin.<br />
C’est-à-dire quand même le développeur va aller mettre les pattes dans la maquette quand même.<br />
Donc, un autre projet qui s’appelle Pure où là finalement on va pouvoir sortir les informations de templating dans un fichier à part.<br />
Donc on met, par exemple si on veut sélectionner la classe product, on met .product.<br />
Tout simplement. Une sorte de foreach. Et puis, on remplit le nom du produit.<br />
Une piste pour un monde meilleur :<br />
Si on peut continuer cette logique là et la porter côté back office, enfin côté métier.<br />
On garde le layout. On refait l’histoire du foreach tout ça comme avant.<br />
Mais on pourrait tester s’il y a un container pour mes produits.<br />
S’il n’y a pas de container, je demande à ma maquette de rendre quelque chose, pour qu’on affiche la liste des produits, bêtement.<br />
Et que c’est pas un contenu principal.<br />
C’est-à-dire qu’on pourrait enlever la maquette et tester le métier.<br />
Et en même temps, enlever le métier et toujours tester la maquette.<br />
Oui ? Non ? [rire]<br />
Là la maquette elle est comme d’origine.<br />
Et là le métier n’est pas lié à la maquette.<br />
Si on l’enlève on peut toujours l’avoir.<br />
Donc on pourrait utiliser un thème par défaut ou voir la maquette comme elle est.<br />
Bon ça c’est dans un monde idéal. Et en attendant, bein il faut d’autres choses.<br />
Donc en attendant on va essayer de revoir un peu les process et voir si on peut pas essayer de changer le rôle de chacun.<br />
Et trouver un système à moyen terme.<br />
Donc à moyen terme, au lieu que la maquette HTML soit passée au développeur est-ce qu’on pourrait pas dire :<br />
Le développeur il travaille avec des frameworks.<br />
Il travaille avec des termes, des CMS, des frameworks qui génèrent du HTML, des CMS qui on des thèmes par défaut.<br />
Est-ce qu’on peut pas dire :<br />
Bon bein le développeur métier il a du HTML valide.<br />
Il le donne à l’intégrateur. Et l’intégrateur HTML finalement c’est un intégrateur pas HTML dans ce cas-là. C’est un intégrateur qui va faire que du CSS.<br />
Il va habiller.<br />
Ce qui donne en fait une solution assez évidente.<br />
Si on prend un système comme ça, le client qui un bug,<br />
Il le passe au chef de projet. Il lui dit il y a un bug.<br />
Le chef de projet il va dire : il y a un bug.<br />
Et le développeur métier il va dire ça c’est IE6. C’est pas à moi.<br />
Si c’est un bug métier, il le corrige.<br />
Si c’est pas un bug métier, il le passe à l’intégrateur qui le corrige.<br />
Pas d'aller-retour. Pas de souci.<br />
Tout ce passe bien dans le meilleur des mondes.<br />
Mais non. Alors c’est une solution qui marche.<br />
J’ai vu des projets passer avec très bien, nickel.<br />
Par contre, ça marche pas sur tous les projets.<br />
C’est-à-dire que l’intégrateur c’est pas un magicien.<br />
Il peut pas faire n’importe quel rendu avec n’importe quel HTML de manière propre.<br />
Donc il faut avoir une bonne coopération entre le développeur et l’intégrateur.<br />
Il faut qu’ils aient l’habitude de travailler ensemble, qu’ils puissent s’appeler facilement.<br />
Ou c’est encore mieux s’ils sont dans un bureau l’un à côté de l’autre.<br />
Et enfin ce qui gêne un peu des fois, c’est que finalement, avec ce système-là on va se retrouver avec un client qui va pouvoir voir le rendu du site qu’à la fin du projet.<br />
Il va pas se retrouver avec une maquette dans nos navigateur qu’il va pouvoir cliquer, changer d’avis tout de suite, depuis le projet "Parfait ! on recommence !".<br />
Là on va avoir fait le métier. Puis on va faire la maquette, par-dessus.<br />
Et puis là, il va changer d’avis.<br />
C’est un peu dommage.<br />
Finalement, j’ai pas parlé du coupable.<br />
Alors qui est le coupable ?<br />
Qui doit-on désigner comme responsable pour changer cette situation ?<br />
Et bein le coupable, j’ai envie de dire, c’est le développeur.<br />
Finalement c’est lui qui tient l’arme du crime.<br />
C’est lui qui a découpé sa maquette.<br />
Et encore pire que ça. J’sais pas si vous déjà vu deux développeurs travailler ensemble. Mais ils travaillent avec des webservices, des interfaces, des APIs.<br />
Jamais un développeur va s’amuser à mettre les pattes dans le code d’un autre s’il comprend pas ce qu’il a voulu faire. <br />
Et là, le travail d’intégrateur, il arrive. Et il prend 5 maquettes et il les transforme en 100 templates.<br />
Et il a aucun souci, aucun scrupule. C’est ses outils.<br />
Est-ce que le développeur aurait pas pu se rendre compte à un moment qu’il fallait qu’il se détache de ce code maquette ?<br />
Et en complice j’ai quand même bien envie de mettre le chef de projet.<br />
Donc soit qui a pas essayé de faire un autre système d’organisation, soit justement qui a fermé les yeux sur le fait que toutes les maquettes étaient découpées en 100 petits morceaux, sans rien faire.<br />
Alors pour résumer un peu ce que je viens de dire :<br />
Donc on a une solution à long terme qui existera pas tout de suite,<br />
Une solution à moyen terme qui est de réorganiser l’équipe.<br />
Mais ça marche pas pour tous les projets.<br />
Donc il faut aussi trouver des solutions à court terme.<br />
Donc il faut de meilleurs outils. Donc ça on a bien vu que ça marchera pas.<br />
Le chef de projet décide, à chaque début de projet, qui va générer le HTML.<br />
Est-ce que c’est l’intégrateur ? Est-ce que c’est le développeur ?<br />
Le développeur découpe raisonnablement sa maquette.<br />
C’est-à-dire qu’il découpe pour factoriser. Donc pour gagner en maintenabilité.<br />
Or, à gagner en maintenabilité en découpant il perd de la maintenabilité.<br />
Donc il y a une limite à trouver. Et pas découper, ouais moi j’ai vu des projets à 200, 300 templates.<br />
Ca commence à faire beaucoup.<br />
L’intégrateur peut essayer de séparer au moins le layout du contenu.<br />
J’ai fait des projets où l’intégrateur faisait bien l’effort de mettre 2 3 include php.<br />
Et finalement, les développeurs et les intégrateurs s’entendent vachement mieux quand ils ont déjà un peu la même logique.<br />
L’intégrateur et le développeur utilisent le même repository.<br />
Alors là il y a… ça va sûrement pas plaire à tout monde.<br />
Mais je l’ai vu en projet et ça se passe bien.<br />
Comme ça, à chaque fois que le développeur il voit qu’il y a un commit du dossier de la maquette, il le met à jour.<br />
Et inversement, si le développeur commence à toucher à la maquette l’intégrateur le sait tout de suite.<br />
Et le site ? Bein le développeur s’il corrige un bug CSS et qu’il a pas le temps de faire un aller retour avec l’intégrateur ou parce qu’il a pas besoin, et bein il le fait dans un fichier à part.<br />
Et il le commente.<br />
Commenter c’est très important parce que il va corriger un bug qui ne va peut-être plus existe après quand on va mettre à jour la maquette.<br />
Et si on sait pas ce que ça voulait résoudre, et bein on va pas réussir à l’enlever.<br />
Donc on va se retrouver avec plein de corrections de bugs qui existent plus.<br />
Donc j’espère que ça vous a plu, un peu.<br />
Si vous avez des questions, c’est maintenant ou plus tard, ou par mail, ou par Twitter.<br />
[Applaudissements]<br />
Question intervenant salle : du coup je me sens un peu accusé là tout de suite [sourire].<br />
T Zilliox : c’était le but. Au moins d’interagir.<br />
Intervenant : j’accepte. Effectivement, en tant que développeur, je découpe des templates et je fais des include dans tous les sens. Parce que ça m’arrange. Voilà.<br />
[hochement d’approbation de T Zilliox]<br />
Après, on s’arrange. J’essaie avec les intégrateurs de leur remonter tous les problèmes que j’ai pus avoir.<br />
Et quand l’intégrateur corrige un bug, il m’en parle. Voilà. Au final chez nous on traite les problèmes avec la communication.<br />
Et ça marche plutôt bien.<br />
Sans avoir des outils évolués. On n’a même pas Git. Et on y arrive. Voilà.<br />
T Zilliox : la communication, du coup tu fais du versioning par mail ?<br />
Intervenant : voilà. Ca peut marcher.<br />
T Zilliox : dès que tu fais une modif t’envois un mail ?<br />
Intervenant : non. C’est en exagérant.<br />
Et voilà. Nous le seul outil ça va être une bonne communication déjà, à la base.<br />
Et l’autre truc c’est : je pense qu’il y ait de solution miracle sur chaque projet.<br />
Quand on bosse avec des personnes qu’on connaît, ça peut marcher.<br />
Même si on change d’intégrateur, de développeur sur chaque projet, les gens travaillent de façon différente.<br />
Ca peut être… Voilà, enfin sur du long terme, de travailler avec des gens différents, ça entraîne du coup une méthodo différente à chaque projet etc…<br />
Donc je suis pas trop d’accord avec les grands points à dire "voilà comment faut faire quoi".<br />
Et l’autre question pour accuser un peu les intégrateurs : <br />
Moi je fais du JavaScript aussi.<br />
T Zilliox : oui.<br />
Intervenant : quand un intégrateur va toucher à mon JavaScript, par exemple, je vais me sentir aussi mal que ce que tu as exprimé tout de suite. Voilà.<br />
Donc est-ce que t’as une méthode, dans l’autre sens, quand l’intégrateur va toucher au JavaScript parce qu’il a modifié un nœud HTML, parce que son menu dépliant doit changer etc ? Enfin voilà.<br />
[T Zilliox esquisse une réponse]<br />
Intervenant : la communication.<br />
T Zilliox : ouais.<br />
Intervenant : sachant que, est-ce qu’un intégrateur, voilà, en parlant du niveau de chacun aussi… Nous on a une problématique en ce moment à l’agence c’est : où est-ce que les intégrateurs s’arrêtent dans le JavaScript, par exemple ?<br />
Quand est-ce qu’on va leur dire :<br />
Ok bein le JavaScript que tu fais, arrête là parce que c’est pas du bon JavaScript. C’est au développeur de le faire, maintenant.<br />
Ca c’est une problématique actuelle qu’on a sur chaque projet. Et on n’arrive pas à la résoudre.<br />
T Zilliox : Alors…<br />
Intervenant : j’ai dit plein de choses.<br />
T Zilliox : tous les projets sont différents. Clairement.<br />
Il y a pas de solution miracle. C’est un peu pour ça qu’il y a eu beaucoup d’idées et pas beaucoup de solutions à utiliser pour de vrai en fait.<br />
Mais la communication ça marche mieux, ouais.<br />
Si on connaît bien l’intégrateur ça se passe vachement mieux.<br />
Moi par exemple le cas qui s’est passé avec IE6, j’ai eu la chance de dire une fois : « tu ne me refais pas une maquette. Tu fais chez toi et après tu m’envoies un mail avec toutes les modifications que t’as faites. »<br />
Donc j’avais un mail avec 50 modifications : ce nœud-là tu le mets au-dessus, cette classe tu la descends.<br />
Bon c’est pas digeste, mais au moins le résultat était bon.<br />
Donc la communication, oui. C'est la meilleure.<br />
Pour découper côté JavaScript, on se retrouve avec exactement les mêmes problématiques.<br />
Mais c’est vrai qu’elles sont moins isolées. Donc c’est un peu plus flou à la limite.<br />
Donc là ouais, c’est pareil. Ca dépend aussi des compétences de chacun. Est-ce que t’as un intégrateur qui est à l’aise avec le JavaScript ou pas ?<br />
Normalement, moi j’ai l’habitude de dire de s’arrêter à l’animation.<br />
Enfin présentation et animation. Mais c’est pareil. C’est-à-dire que la fois où ça s’est bien passé, c’était aussi avec un repository en commun.<br />
C’est-à-dire qu’à chaque fois qu’ils modifiaient le HTML, nous on pouvait ajuster tout de suite le tir.<br />
C’est-à-dire que, éviter d’avoir le slider, la démo qui marche plus parce que bein il y a un div en plus autour du header, qui est arrivé…</p>
