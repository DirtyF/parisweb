Déjà que pour un adulte c’est pas évident de comprendre.<br />
Je dis ben tiens. Qu’est-ce qui existe sur le web ?<br />
Et par exemple, avec des applications comme ça, je peux lui montrer que Mars ça tourne beaucoup plus vite que la Terre.<br />
Et que Mars est quand même beaucoup plus petite que Vénus.<br />
Et qu’une planète gazeuse ça ressemble pas à une planète comme la nôtre.<br />
Et donc là, ben directement, dans le navigateur…<br />
Puis en plus j’en avais trouvé une autre.<br />
Bon, là je vous l’ai pas mise parce que je peux pas tout montrer non plus.<br />
Mais carrément on interagit :<br />
On regarde la Lune qui tourne autour de la Terre.<br />
Sur d’autres… voilà sur d’autres exemples, c’est intéressant.<br />
Ensuite, ben par exemple la représentation de données.<br />
Ça j’aime beaucoup par exemple.<br />
Donc là c’est une représentation qui vous donne une idée du volume de population sur la Terre en 90.<br />
Et puis hop ! On passe en 2000.<br />
Voilà.<br />
Alors là, une autre représentation de données.<br />
Ouais ça a l’air de bien charger là.<br />
Là ici par exemple, sur celle-là, on va vous montrer les tempêtes qu’il y a eu sur Terre sur une période donnée.<br />
Donc, en bas vous avez une timeline.<br />
Vous pouvez vous balader sur la timeline et puis directement vous allez voir le truc.<br />
Donc là, ben je suis obligé de conclure.<br />
Donc j’arrive à la fin hop hop !<br />
Donc WebGL, moi j’ai été scotché quand j’ai pu voir tout ça.<br />
Et franchement, là on a quelque chose qui est standard.<br />
Et certes, c’est jeune, mais au niveau des possibilités, vous avez quelque chose d’énorme entre les mains.<br />
Donc, pour peu que vous soyez développeur, que vous vous intéressiez à ce genre de choses, au niveau de l’interactivité d’un navigateur, c’est vraiment très très fort.<br />
Donc jetez un œil à ce qui se passe.<br />
Il y a des choses qui sont de plus en plus nouvelles, et vraiment très très intéressantes.<br />
Je vois des expériences qui sortent, je dirais presque tous les jours.<br />
Et vraiment il y a du bluffant, il y en a énormément sous la pédale.<br />
Donc moi je vous dis aujourd’hui : inventez ce qui va avec !<br />
Parce que là, on a un outil supplémentaire, il est excessivement puissant.<br />
Je le trouve très prometteur.<br />
C’est pas pour rien que le jeu vidéo s’en empare de façon très très forte, parce que là-dessus ils sont souvent très en pointe.<br />
C’est ouvert.<br />
Voilà, donc je vous remercie pour votre attention. [Applaudissements]<br />
Et puis, soyez curieux !<br />
Allez voir ce qu’il y a sur le web !<br />
Parce que franchement il y a des choses qui sont très très bluffantes.<br />
Voilà, merci beaucoup. Au revoir.<br />
