    <div class="talk-description"><p>Stéphane vous propose de faire un petit tour d'horizon de l'arrivée de la 3D en temps réel dans le navigateur grâce à la finalisation récente de WebGL 1.0.<br />
Les navigateurs modernes ont pour la plupart implémenté cette technologie et au-delà de l'expérimentation, les premières utilisations pratiques peuvent être aujourd'hui envisagées.</p>

<p>Après un bref retour historique afin de contextualiser WebGL, il vous fera entrer dans le vif du sujet et vous montrera rapidement son fonctionnement et son interaction avec le navigateur.<br />
Quels sont les prérequis techniques pour que WebGL soit activé dans le navigateur ? Qu'est-ce qu'une image 3D et comment, par l'intermédiaire de Javascript et d'une bibliothèque telle que ThreeJS, les fonctions de WebGL sont activées pour former une image 3D dans le navigateur.</p>

<p>Pour illustrer le tout, il vous présentera des exemples d'expérimentations représentatives de WebGL en action, avec quelques exemples utilisés aujourd'hui en production.</p></div>
