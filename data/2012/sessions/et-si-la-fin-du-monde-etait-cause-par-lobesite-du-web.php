    <div class="talk-description"><p>Le logiciel et les sites web respectent une loi toujours vérifié : l'entropie. Plus il y a de place et de ressource, plus le logiciel va l'occuper. Les impacts sont énormes : obsolescence du matériel, baisse de performance, consommation des data centers…</p>

<p>Pourtant des pratiques  existent pour limiter ce phénomène : éco-conception, meilleure qualité, green pattern… Ces pratiques peuvent de plus être un atout économique et social pour le monde du web.</p>

<p>Alors pourquoi s'en priver ?</p></div>
