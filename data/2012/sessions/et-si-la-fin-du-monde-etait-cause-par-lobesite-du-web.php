		<header><h2>Transcription</h2></header>
		<p>(Merci à Tanguy Lohéac pour la transcription.)</p>

<p>Un titre un peu catastrophique à la Roland Emmerich : 2012.<br />
Je suis un peu déçu parce que j’ai un quart d’heure pour vous parler de la fin du monde, lui il a mis 1h30.<br />
Alors le Green Code Lab on est une association.<br />
On travaille dans l’éco-conception des logiciels, des green patterns.<br />
On regroupe des développeurs, aussi des experts du développement durable.<br />
Et notre objectif c’est un peu d’analyser est-ce que les technologies ont un impact sur l’environnement. Et si elles en ont comment on peut faire pour limiter cet impact, et pas forcément aussi que sur l’environnement : sur le social.<br />
Notre message c’est pas de dire : les technologies c’est mal c’est sale ça pollue.<br />
C’est de dire ça a une force extraordinaire, on l’a vu sur des jeux sur le social.<br />
Par contre ça serait dommage d’avoir des impacts plus négatifs pour faire des choses.<br />
Donc voilà, on a rédigé un livre qui est un manuel sur l’éco-conception, pas forcément à destination que du Web mais des gens du logiciel ou des gens qui tournent autour du monde du logiciel.<br />
Alors premier constat, c’est que la terre c’est un système fini, c’est un écosystème fini.<br />
On pourrait penser que on a des ressources illimitées.<br />
Globalement toutes les ressources sont finies, donc c’est vrai, à notre échelle de 60 ans ben on pense que ça sera illimité mais globalement, quand on regarde, c’est une étude très sérieuse du Global Network Footprint, qui a regardé l’empreinte de notre consommation, combien on consomme en ressources.<br />
On est actuellement à la consommation d’un équivalent de 1,3 planètes.<br />
C’est comme si vous aviez quelque chose de fini et vous en consommez plus.<br />
Il est notre notion aussi, c’est le jour du dépassement.<br />
C’est-à-dire qu’ils ont dit :<br />
À partir de tel jour, on consomme plus dans l’année que ce qu’on a.<br />
Et globalement la moyenne mondiale elle est en juillet.<br />
C’est-à-dire qu’à partir de juillet on surconsomme.<br />
Les États-Unis sont à 5, l’Europe est à 3.<br />
Il y a des pays qui font moins mais qui approchent.<br />
Donc voilà, on consomme trop !<br />
Après les TIC dans tout cela ? Les technologies de l’information, on se dit c’est virtuel, c’est pas nous qui polluons.<br />
C’est l’aviation, c’est l’agriculture.<br />
Nous on est totalement virtuel, donc on n’a rien à se reprocher.<br />
Autre étude : on est responsable de… Enfin toutes les technologies les Smartphones, les data Center c’est 2 % des émissions de CO2 mondiales.<br />
2 % quand on regarde, c’est l’équivalent des transports aériens.<br />
Voilà donc sauf que le problème c’est que le transports aériens, cette consommation elle est stabilisée.<br />
Et nous on explose en fait, là c’est une étude qui a deux ans, je pense qu’on referait l’étude aujourd’hui, ça serait l’équivalent de 4 % d’émissions de CO2.<br />
Donc on est sur une sorte de croissance de l’impact des technologies.<br />
 Et c’est normal parce que avant, on n’avait pas autant de Smartphones, on n’avait pas autant de matériel, il y a des Data Center qui arrivent.<br />
 <br />
Donc c’est un domaine qui croît et qui impacte.<br />
Alors, est-ce que c’est vraiment un problème ?<br />
Et pourquoi c’est un problème ?<br />
Quand on regarde pourquoi l’informatique pollue c’est la notion de sac à dos écologique.<br />
Un sac à dos écologique c’est-à-dire que pour faire voilà, une table, pour faire 1 kg de table vous avez besoin de…<br />
Pour faire 1 kg de table vous avez besoin de plus de matière, des minerais de l’eau de l’énergie.<br />
Et ce sac à dos écologique, quand on le regarde sur une voiture, pour 1 t de voiture, il faut 60 t de matières diverses.<br />
Pour 3 kg de portable il faut 10 t de matières.<br />
Donc voilà, proportionnellement on est sur des technologies et les matières qui utilisent beaucoup plus de ressources.<br />
Et si on prenait juste la puce électronique c’est 1 sur 16 000.<br />
Donc c’est énorme !<br />
Donc tout ça fait que avec cette croissance des technologies il y a un impact énorme.<br />
Pourquoi aussi… La loi de Moore je sais pas si vous connaissez ?<br />
Moi je viens plutôt de l’électronique donc on…<br />
La loi de Moore, c’est Gordon Moore, un ingénieur d’Intel qui a regardé dans les années 60 la croissance des puces électroniques.<br />
Il a dit : tous les 18 mois il y a quelque chose qui double.<br />
Et on a vérifié cette loi.<br />
Là c’est le nombre de transistors dans un processeur, ça double tous les 18 mois.<br />
Donc, c’est exponentiel.<br />
Et on a toujours plus de ressources.<br />
Voilà, les PC d’il y a 10 ans étaient moins puissants.<br />
Les PC dans 10 ans, normalement, si on suit la loi de Gordon Moore, ils vont continuer à croître.<br />
Notre problème c’est que voilà vu qu’on a toujours plus de puissance, on n’optimise pas, dans le monde du logiciel.<br />
Pourquoi optimiser nos logiciels puisque dans 18 mois on aura toujours plus de puissance ?<br />
Et globalement, le logiciel il suit une obésité, et le Web c’est pareil. Ce qu’on appelle l’Obésiciel.<br />
Vous pouvez le voir sur Windows hein.<br />
Entre le Windows il y a 10 ans et Windows maintenant, on a multiplié les ressources par 100.<br />
Est-ce qu’il y a plus d’utilisations ?<br />
Est-ce qu’on a plus d’usages ?<br />
Pas plus.<br />
Word il y a 10 ans faisait pareil.<br />
Voilà, j’ai pas plus d’usage mais j’ai eu une croissance.<br />
Autre loi : donc c’est une loi de Wirth<br />
Donc c’est pareil : c’est le logiciel ralentit plus vite que le matériel accélère.<br />
Vous pouvez prendre tous les projets, n’importe quel domaine, ça suit ces lois.<br />
Après il y a eu la solution magique !<br />
C’est vrai que maintenant avec le Web, et surtout le cloud,<br />
Tout ce matériel qui est polluant, on va pas le laisser dans nos sociétés, on va miniaturiser, on va envoyer ça dans le Web.<br />
Sauf que, si vous vous rappelez Lavoisier "Rien ne se perd rien ne se crée".<br />
Le problème il est pas résolu.<br />
C’est-à-dire que les puissances qu’il y avait dans les serveurs dans les sociétés, elles sont externalisés et elles sont quelque part dans le Web.<br />
Si on regarde un exemple : Facebook.<br />
Bon, sur le nombre des serveurs ça peut-être évolué.<br />
C’est l’équivalent de 9 MW. <br />
C’est la consommation d’un TGV 24 heures sur 24.<br />
Voilà, derrière il y a quand même un impact. C’est pas du virtuel.<br />
C’est quelque chose de vraiment réel.<br />
Avec un impact sur l’environnement, sur le social.<br />
Et on peut dire : d’accord c’est mutualisé pour des millions d’utilisateurs, mais globalement c’est la petite goutte qui fait le fleuve.<br />
Donc, on prend Facebook, on prend tous les services Web, derrière il y a des choses qui sont très matérielles et on est pas dans un monde virtuel !<br />
Autre constat :<br />
Une recherche Google. Donc là c’est une recherche « ADEM3 ».<br />
C’est l’équivalent de 10 g équivalents CO2. <br />
Donc c’est une ampoule allumée pendant 1h, quand vous faites une recherche.<br />
Ou l’extraction de ressources non renouvelables donc en fer :<br />
6 g. Donc c’est l’équivalent d’une pièce de un euro.<br />
Donc on extrait des ressources à chaque fois qu’on recherche.<br />
Alors le Web dans tout ça ?<br />
Là c’est un projet, je sais pas si vous connaissez ?<br />
C’est HTTP Archive qui archive la performance des sites Web.<br />
L’échelle c’est un an.<br />
Et donc sur la totalité des sites archivés, ben on voit qu’on a une explosion de la taille des requêtes, de la taille des pages.<br />
Donc cette obésité on l’a aussi sur les sites Web.<br />
Là je vais revenir sur les bonnes pratiques.<br />
Parce que c’est pas… Le constat il paraît dramatique mais en même temps les solutions sont très simples.<br />
Très simples ?<br />
Revenir sur les besoins.<br />
On parlait sur l’ergonomie.<br />
Mais le besoin c’est, on va dire, on peut diminuer de 80 % l’impact de nos sites et des serveurs si on repense aux besoins.<br />
Google, ou Bing, il y a une vraie étude qui a été faite sur ça de dire :<br />
Plutôt que de fournir et d’aller chercher des millions de résultats en un clic, pourquoi pas juste fournir 20 résultats ?<br />
Ou la première page ?<br />
La loi de Pareto : 80 % des utilisateurs de toute façon ne vont pas sur la deuxième page.<br />
Donc on diminuerait côté serveur, et Microsoft a fait l’étude, de 80 % l’énergie consommée.<br />
Donc voilà, revenir sur le besoin, re-réfléchir aux besoins permet de faire quelque chose plus les pieds sur terre.<br />
On a des logiciels aussi, et des sites Web, qui sont pas forcément intelligents.<br />
Alors là, c’est pas de la science-fiction.<br />
On arrive à mesurer la consommation d’un site, la côté client.<br />
Là c’est ma plate-forme : 22 W.<br />
Je charge mon site. Je minimise la fenêtre.<br />
Et en fait je reviens plus à 22 W je reviens à 23 W.<br />
Pourquoi ? Ben parce que derrière, on a des scripts qui continuent à tourner, des publicités.<br />
Donc là c’est hyper simple !<br />
C’est-à-dire que ben je prends le contexte :<br />
Si l’utilisateur minimise sa fenêtre, j’arrête tous mes scripts.<br />
Donc, je sais pas, on peut faire le sondage : qui fait ça ?<br />
Une personne.<br />
Elle a gagné le bouquin. Je pense. [Rire]<br />
Donc voilà. Mais on se dit c’est qu’un watt, c'est que quelques méga, mais si on multiplie sur les applications, sur les onglets, globalement c’est ce qui fait que cette obésité on la ressent.<br />
Faut gratter un peu partout !<br />
Après, qui paye ?<br />
On s’est pas synchronisé mais Angry Bird :<br />
Il y a une étude américaine qui a regardé l’énergie consommée par toutes les méthodes.<br />
Globalement qu’est-ce qui consomme ?<br />
C’est 75 % de l’énergie consommée par Angry Bird c’est pour la géolocalisation, pour la publicité, pour la récupération d’informations.<br />
Après, c’est aussi, je vais pas rentrer, c’est les IO.<br />
Voilà, c’est toute cette partie.<br />
Mais si vraiment on regarde la partie utile d’Angry Bird, c’est que 25 % de l’énergie.<br />
Le reste c’est on va dire, du surplus par rapport aux besoins.<br />
Voilà donc on peut réfléchir aussi sur ça.<br />
Qui paye ? C’est globalement tout le monde.<br />
Et surtout l’utilisateur hein.<br />
Si ces gratuits vous êtes dans le produit !<br />
Donc là c’est ça. C’est vous qui payez sur l’énergie.<br />
Pour aller rapidement et conclure.<br />
Le constat comme je disais est un peu catastrophique.<br />
Mais quand on travaille avec des développeurs, que ce soit les techno PHP ou des techno Java ou des techno C++, on voit des pratiques qu’on avait oubliées.<br />
Moi je viens du monde de l’embarqué, en fait.<br />
Donc il y a 15 ans, je programmais sur des petites choses.<br />
On apprenait à optimiser.<br />
Là, vu qu’on a cette explosion de puissance il y a plus besoin d’optimiser.<br />
Par contre il faut juguler un peu cette explosion.<br />
On va dire, c’est la poule ou l’œuf :<br />
Est-ce que c’est le matériel ou le logiciel qu’il faut stopper ?<br />
Chacun doit faire son travail pour arrêter ça.<br />
Et si je reviens sur la loi de Gordon Moore.<br />
Gordon Moore il est revenu il y a cinq ans sur ce constat.<br />
Il a dit : attention ! Moi dans les années 60 ce que j’ai vu sur l’explosion du matériel c’était pas une loi physique. C’était juste un constat.<br />
Et pour moi, ma loi c’est une prophétie auto-réalisatrice.<br />
C’est-à-dire que l’industrie du matériel met les efforts pour toujours plus de puissance. <br />
Donc, on met le marketing, on met la R&D pour avoir toujours plus de puissance.<br />
Et globalement, nous développeurs, n’arrive plus à suivre cette puissance.<br />
Enfin, qui développe sur des cœurs parallèles ?<br />
Donc globalement, on a des cœurs parallèles mais qui consomment presque autant qu’un simple cœur.<br />
Voilà. Pensez à l’obsolescence hein !<br />
Pas simplement l’énergie.<br />
Dans l’association on a des gens qui sont très radicales.<br />
C’est-à-dire qu’ils prennent des PC reconditionnés, des PC qui ont 10 ans et ils essayent d’aller sur un site Web :<br />
Comme réserver un billet sur voyages-sncf.<br />
Ils peuvent plus en fait.<br />
Ils peuvent plus parce qu’il faut réinstaller ça, ça, ça.<br />
Ça tourne plus.<br />
Donc alors ils se retournent vers d’autres sites.<br />
Alors il y a un autre site qui est Capitaine Train.<br />
Voilà, ils arrivent à réserver comme ça.<br />
Mais c’est vraiment une lutte de tous les jours pour essayer d’éviter cette obsolescence.<br />
Et on parle beaucoup d’obsolescence programmée avec un complot des fabricants qui mettent des petits compteurs.<br />
Globalement l’obsolescence, c’est plutôt les développeur...Enfin je jette pas la pierre hein, moi aussi j’ai du mal à éviter cette obsolescence, mais qui l'a programmons aussi.<br />
Donc voilà. <br />
[Applaudissements]<br />
Trois actions :<br />
Donc Green Pattern je vous invite.<br />
Ça permet de réfléchir un peu.<br />
Ça casse pas des briques hein.<br />
Mais on a posé le domaine un peu.<br />
Et on invite les gens. On veut faire une V2.<br />
Sur Green Pattern. Une V2, on va dire, avec plus de consensus.<br />
On a validé en fait un livre sur un référentiel.<br />
Donc pareil je pense que les auteurs seront OK pour faire une V2.<br />
C’est déjà une base.<br />
Et un autre projet qui est financé par l’ADEM :<br />
Je fais un peu de teasing. Il va sortir dans quatre mois. On le développe.<br />
C’est à la manière de http Archive : Web Energy Archive.<br />
On va archiver l’énergie de vos sites Web.<br />
Donc ben je vous invite, quand ce sera mis à mettre votre site Web.<br />
Et puis vous pourrez vous positionner.<br />
Et là on a des trucs un peu voilà.<br />
C’est tous les sites, et on voit qu’on a des différences entre sites Web.<br />
Et c’est intéressant. Ça permet de… Voilà. D’évoluer.<br />
Merci.</p>

