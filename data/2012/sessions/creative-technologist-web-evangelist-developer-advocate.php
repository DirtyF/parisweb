	<article id="transcription-679">
		<header><h2>Transcription</h2></header>
		<p>Bonjour, je suis là pour vous parler du poste de creative technologist, enfin tel que nous on l’entend. Alors j’ai mis webevangelist et developper advocate parce que pour moi le rôle de creative technologist, enfin pour nous, c’est un peu le pendant en agence ou chez les intégrateurs de ces différents rôles. Concrètement, je vais parler du rôle de creative technologist chez nous ; c’est-à-dire chez Ekino. Moi mon poste c’est creative tech chez Ekino. Ca fait quelques années que j’y suis. Ekino c’est boîte d’environ 150 personnes à Paris, on va dire au service d’une intégration web de qualité. Dans cette agence – enfin dans cette agence – chez Ekino on est environ 40 personnes rien que dans l’équipe front dont je fais partie.</p>

<p>Je vais essayer de vous expliquer un peu pourquoi est-ce qu’on a voulu créer un poste de creative tech chez Ekino et qu’est-ce qui nous a amené à créer ce rôle. Concrètement, on a fait un constat, c’est que les technos ont évolué. La techno a évolué, la techno évolue encore. Et il va falloir faire face un p’tit peu. On est face à une multitude de frameworks. On est face à des nouveaux process, des nouvelles choses. On est face à une nouvelle complexité à laquelle on n’est pas encore tous prêts que ce soit côté des clients et aussi côté des développeurs et chez nous. Et puis on est face à un ensemble de nouveautés, que ce soit les nouveaux OS mobiles, que ce soit Win 8, que ce soit les objets connectés etc. Et on est en face d’une diversité de devices complètement hallucinante et qu’il va falloir expliquer aux gens, qu’il va falloir nous-mêmes comprendre en interne pour pouvoir avancer sereinement dans l’innovation digitale.</p>

<p>Un autre constat : les développeurs ont changé. Alors bon, je barre un terme intégrateur qui est quand même beaucoup utilisé encore. Mais concrètement ce que ça veut dire, pour nous en tout cas, les développeurs sont plus des intégrateurs, c’est vraiment des véritables experts de leur technologie. Et comme bons experts, ils ont peut-être du mal parfois à prendre une certaine hauteur par rapport aux projets parce qu’ils sont tout le temps dans leurs projets. Ils sont face à des supports multiples. Ils ont vraiment les mains dedans et c’est parfois un parcours du combattant finalement d’être développeur, en agence. Parce que, on a la tête dans le guidon concrètement quoi. J’étais développeur avant et je sais ce que c’est que de ne vraiment pas avoir le temps de lever le nez. </p>

<p>Un autre constat : on est face à des clients qui changent, qui ont changé. Et c’est assez impressionnant, c’est assez le big bang. Déjà ils sont ultra-connectés : ils ont un Iphone, ils ont un Ipad, ils ont un Mac, ou un PC bien sûr [léger sourire]. Ils sont technophiles, ils sont geeks. Mais en fait un peu comme tout le monde, enfin comme de plus en plus de personnes dans notre société. Et malgré tout, ils ont un vernis technique. Et ils ont besoin de comprendre certaines choses. Ils ont besoin qu’on leur explique, tout en étant hyper connectés. Donc ils vont arriver avec des questions vraiment pointues. On va avoir des clients par exemple qui vont nous dire… Enfin, il y a deux ans, on nous disait il faut pousser le HTML5 sur le mobile. On avait des clients qui nous voulaient absolument ça : du html sur du mobile etc.… Puis, maintenant, il y a Facebook qui est revenu en arrière et qui fait du natif sur le mobile. Et on a donc des clients qui arrivent en nous disant : « ben non euh ! Nous on veut plus du tout de HTML5 sur le mobile… ». Bref, ils arrivent avec une idée super préconçue de ce qu’est le web et de ce qu’on va leur donner. Et donc va falloir leur expliquer tout ça.<br />
Malgré tout, ils ont un désir d’innovation, et on essaie de les accompagner là-dedans. Et c’est assez nouveau. Avant on faisait des sites pour eux qui essayaient de reprendre un peu leurs besoins etc.… Ils arrivaient avec une expression de besoins assez simples, et ils n’étaient pas forcément dans un désir d’innovation pour se démarquer par rapport aux autres.</p>

<p>C’est simple, pour innover, pour avancer, les acteurs, donc nous, on doit s’adapter. On doit s’adapter, et pour s’adapter, l’idée c’est de créer un p’tit peu, enfin ça été notre idée en tout cas, de créer différents métiers, un peu à la confluence des autres, histoire de rajouter du liant et d’aider nos clients qui, finalement avaient des questions et avaient besoin d’avoir des réponses. Et donc, dans ces nouveaux métiers, on a créé finalement un métier qui était capable de faire le liant entre le marketing, - c’est un bien grand mot, j’y reviendrai mais… - la technologie, et la créativité. Et tout ça au service du client. Le rôle du creative tech c’est clairement d’être à la confluence de ces métiers-là. C’est pas d’être le meilleur technicien, c’est pas d’être le meilleur marketeur, c’est pas d’être le meilleur finalement en termes de créativité et de tendances. En revanche, c’est capable d’aller voir les bonnes personnes au bon moment, et d’aller voir les bons besoins au bon moment. </p>

<p>J’m’explique. Le marketing, bon on sait grosso modo ce que c’est que le marketing. Le marketing ça va consister à mettre en face des besoins du client une innovation. Clairement le rôle du creative tech dans cette phase là ça va aller être de comprendre le client, d’aller voir son besoin, d’aller au plus près du besoin du client, d’essayer vraiment de rentrer et d’essayer vraiment de comprendre avec lui. Ça va être justement parfois avec l’analytics et avec d’autres données métier, ça va être de s’associer avec des consultants UX qui vont l’aider à mettre en place une stratégie de contenu. Ou, ça va être de s’entourer de consultants marketing qui eux, vont apporter vraiment une idée du marché différente de la sienne.</p>

<p>L’idée aussi du creative technologist, il y a techno dans « technologist », c’est d’être proche de la techno. C’est pas, comme je disais, d’être le meilleur technophile. Mais ça va être par exemple de se rapprocher des développeurs front, qui eux sont les véritables experts, et d’aller voir ce qui se passe de mieux, de les aider, de recueillir parfois leurs doléances sur des trucs qu’ils ont testés mais sur lesquels ils ont pas forcément eu le temps d’avancer, je sais pas. Ça va être de voir avec les archis pour les innovations qu’on va pouvoir mettre en place. D’aller voir avec les développeurs back le lien qu’on peut créer entre le front et le back qui sont en train finalement de se rapprocher de plus en plus, maintenant que ce sont souvent des développeurs front qui font des interfaces applicatives. Et ça va être aussi parfois d’aller chercher l’innovation ailleurs.</p>

<p>Concrètement on peut pas tous être experts de tout. Je sais que par exemple récemment on a eu une demande qui concernait de la reconnaissance d’émotions. J’suis pas spécialiste de la reconnaissance d’émotions. Et chez nous on n’avait personne qui était spécialiste de ce genre de choses. L’idée ça va aller être de s’entourer des bonnes personnes pour ces choses-là. Ça va être aussi de respecter les tendances, d’aller voir les personnes dont les tendances les concernent etc… Et d’aller voir ce qui se passe ailleurs.</p>

<p>Donc c’est un métier où il va falloir se rapprocher des créatifs. Il va falloir se rapprocher des consultants UX, sur les nouvelles ergo, sur tout ce qui se passe un p’tit peu sur le métier, aussi d’aller voir les innovations qui se passent chez les développeurs. Concrètement quand on parle des nouvelles innovations qu’on a vues ces années, que ce soit le parallaxe et tout ça, finalement, c’est souvent des trucs qui sont à la confluence entre l’UX, entre les créatifs et entre les développeurs. Et c’est important parfois d’avoir un peu de recul sur ces points. Qui s’avère être de faire de la veille, et de participer à des conférences forcément.</p>

<p>Donc en fait j’ai essayé de mettre le quotidien du creative tech’ autour de 3 grands axes. Pour moi c’est la veille. C’est super important, c’est le premier rôle. Alors c’est vraiment un poste de veille. Il faut être en dehors du projet. C’est vraiment important d’être en marge de ce projet pour pouvoir aider au mieux le projet. Parce qu’il va falloir orienter la veille. La veille faut pas aller dans tous les sens. En tout cas, moi ça me paraît important finalement, sans vouloir frustrer les gens, d’avoir une orientation de la veille intéressante. Ça va être de créer des outils de veille. Donc, ça peut être des blogs, faire des trucs en interne, des outils en interne qui vont permettre de partager la veille, histoire de pas la perdre et de garder une emprunte de cette veille. Chez nous par exemple, avant on s’envoyait des mails pour se dire : « c’est génial ce site, il est super » etc… Puis au final ça se perdait dans notre pile de mails. On n’était pas capable finalement de revenir dessus. L’important ça va être de créer des outils qui vont permettre de rendre ça plus simple.</p>

<p>Ce métier, clairement, et c’est pour ça que beaucoup de creative technologists viennent ou bien de la créa ou bien de la technologie même, ça va être de tester des choses. Donc, tester des tendances, mais aussi tester des frameworks. Et quand je parle des frameworks, va falloir aller assez loin. Va falloir faire des POC (Proof of concept). Quand je parle de POC, vraiment prendre une technologie, l’essayer, aller jusqu’au fond de la technologie, et essayer de voir ce qu’on peut en retirer. Et peut-être aussi voir les échecs liés à cette technologie-là. Tous les frameworks ne sont pas bons. Parfois on fait des choix. Et c’est important finalement de s’en rendre compte avant les projets.</p>

<p>Ça va être d’évangéliser. Alors l’évangélisation c’est important. Parce que c’est qu’une évangélisation qui va se passer en interne. Donc c’est déjà quelque chose qui est indissociable de la veille. L’évangélisation c’est finalement faire ressortir ce qu’on a vu dans la veille. Mais ça va être de faire de la formation interne pour qu’on ait tous une vision commune, dans les agences ou dans les entreprises de ce qu’on veut faire et de la manière dont on veut innover, et des différentes technologies, et comment est-ce qu’on va voir ces différentes technologies. On peut pas avoir plusieurs a priori sur une technologie, comme par exemple le HTML5. Parfois va falloir arriver avec tous le même discours. Et puis ça va être aussi faire de l’évangélisation client. On est face à des clients qui, comme je le disais, très technophiles et qui comprenaient beaucoup de choses. Maintenant on a aussi des clients à qui on balance des mots, des buzzwords parfois comme « HTML5 », qui finalement sont un petit peu difficiles à appréhender. Parce que, moi j’ai des clients par exemple, en voyant HTML5, eux ils pensent à quelque chose de très créatif, ils pensent à des trucs de paralax, etc. Mais ils se rendent pas forcément compte que derrière HTML5 il y a aussi plein de nouveautés qui sont cachées qui vont être liées à l’applicatif ou qui vont être même liées à la sémantique. C’est hyper important pour eux finalement.</p>

<p>Le rôle du creative tech’ il se passe aussi au moment de la compétition. Alors, la compétition c’est quelque chose qui est très propre aux agences et aux boîtes qui travaillent finalement avec des clients. La compétition c’est quelque chose où finalement ça va se passer dans un contexte d’urgence. On va arriver avec un besoin un lundi, va falloir y répondre le jeudi. Et aujourd’hui on se retrouve avec des clients qui finalement avant avaient besoin d’une vision marketing pendant cette compète et avaient besoin juste d’une créa’, enfin pas juste une créa, mais une créa. Maintenant on doit leur apporter une bonne ergo (donc ça, ça va être le rôle des UX etc.), mais aussi une vraie valeur ajoutée technique. Parce que c’est ça finalement qui va nous permettre de leur montrer qu’on a une vraie compréhension des enjeux techniques. Et c’est ça qui va nous permettre d’innover et de proposer des innovations au cœur même de la compétition. Et finalement c’est peut-être notre moyen et c’est notre seul levier pour faire en sorte qu’on fasse des projets innovants. Et clairement, moi mon rôle, c’est d’essayer d’apporter des projets innovants à l’agence pour que les développeurs bossent sur des projets sympa quoi ! Donc, bon, ça se passe tout de suite à la compet’.</p>

<p>J’vais aller vite, j’vais pas aller vite parce qu’au final, c’est la fin [rire de Florient et de la salle]. Mais grosso modo l’idée c’est de dire – 15 minutes c’est compliqué [sourire complice avec la salle] – la conf’ là elle se passe autour de 3 mots : l’important c’est d’avoir finalement suffisamment de recul pour voir toutes les facettes du prisme. Et donc ça, ça va être le rôle du creative tech, c’est finalement… Comme il n’a pas la tête dedans, il va finalement se permettre de tester des frameworks qui sont pas forcément liés à un projet qui est courant. Et donc il va peut-être pouvoir aller voir d’autres choses et apporter d’autres choses. Parce que demain, peut-être qu’on arrivera avec un projet où on aura besoin de ce nouveau truc. Le mur ? Ben va falloir éviter de foncer dans le mur. Et je pense que ça, ça rassure vraiment les clients. Ça rassure vraiment les agences aussi. C’est qu’on a tous fait l’expérience, si vous voulez, d’un projet où on a dû revenir en arrière parce qu’on s’est rendu compte que la solution technique choisie était pas la bonne. L’intérêt de faire de la veille en amont et l’intérêt de garder cette veille, et l’intérêt finalement d’essayer de la matérialiser, c’est que ça va nous permettre d’éviter ce genre de problématique. Et ainsi d’anticiper les différentes problématiques techniques qu’on va trouver. C’est-à-dire que… ce que je disais tout à l’heure par exemple, par rapport à la reconnaissance d’émotions. Aujourd’hui c’est des problématiques qu’on ne connaît pas. On sait pas très bien comment ça se passe, etc. Et son rôle ça va être justement de voir là où il peut y avoir des problèmes et quelles sont les clés du succès de son projet.</p>

<p>Donc concrètement, l’idée c’est de répondre sereinement aux contextes d’innovation et aux innovations qu’on a envie de mettre en place dans la boîte. Et aussi ce qu’on a envie de mettre en place pour son client. Donc en fait finalement tout ça, on pourrait… Enfin moi j’aime pas trop dire que c’est un métier. C’est plus finalement une méthodologie à mettre en place, dont le creative technologist fait partie, dont le consultant UX fait partie, dont d’autres métiers font partie. Mais finalement c’est de réunir tous ces ensembles de pôles d’innovation pour faire quelque chose.<br />
Donc c’est des innovations marketing. Parce que finalement, il y a beaucoup de choses qui partent de là. Il y a beaucoup de choses qui partent du client. Il y a beaucoup de choses qui partent du marché. Il y a les innovations technologiques. Parce qu’il faut pas les oublier. Faut les présenter. Il y a les innovations créatives. C’est important aussi de montrer au client qu’on peut faire des choses jolies et qui sont intelligentes.  Et c’est en utilisant ce type de méthodologie, à mon sens, qu’on va arriver à des dispositifs innovants, des dispositifs digitaux ou pas digitaux. </p>

<p>D’ailleurs j’ai essayé de mettre des exemples qui sont pas forcément des dispositifs digitaux. Mais comme Tesco qui a créé un système dans le métro où on a juste à scanner un QR-code, et ça rajoute quelque chose dans notre panier et on peut directement acheter un élément directement dans le métro. Ça va être ça. Ça va être – j’ai appuyé sur le mauvais bouton ? – ça. Ça c’est une interface tactile transparente qui est liée à de la domotique où on va pouvoir faire tourner la bouteille en se basant sur un écran transparent etc… Donc, c’est joli. C’est technologique. Et finalement c’est assez innovant. Et puis ça se remet dans un contexte marketing.<br />
Et pour finir, ça peut aller jusque-là. Alors évidemment c’est l’exemple un peu extrême. C’est que finalement le morphing 3D c’est exactement ça ! C’est de la créativité, de la technologie, et au service du marketing. Parce qu’en général, c’est lié à une marque. Là c’est pas lié à une marque : c’est la fête des lumières à Lyon. Mais bon… Ça donne quand même une idée un petit peu de ce que c’est quoi [pause prolongée].</p>

<p>Voilà. [applaudissements francs de la salle]</p>

<p>Question de la salle : concrètement vous intervenez comment dans les compétitions ? Vous intervenez avec l’équipe en présentation ? Ou plutôt en préparation de la réponse au client ?</p>

<p>Florian : les deux. Les deux. On est dans une agence où les structures techniques et marketing sont un peu séparées. Mais l’idée c’est d’intervenir justement dans le deux. C’est-à-dire que, bon, parfois on a des slides tout fait. Ça existe. C’est comme ça. Parfois on a des problématiques où on a déjà des slides. Mais l’idée c’est aussi d’aller rassurer le client. Et finalement, si on avait besoin juste de slides, on pourrait se permettre de demander ça à des experts qui sont un petit peu sous-marins. C’est un peu ce qu’on faisait avant. Finalement, on allait demander à des développeurs comment est-ce qu’on allait faire techniquement. Maintenant finalement on a des clients – c’est peut-être pas bienvenu mais… - on a des clients qui ont vraiment besoin d’avoir un contact humain, avec quelqu’un qui comprend la technologie, mais qui est capable de lui parler. Et qui est capable de [réaction manifestement vexée de quelques-uns dans la salle]. Excusez-moi [rire un peu gêné] !</p>

<p>Non, mais je ne dis pas que tous les développeurs savent pas parler à des clients. Loin de là. Mais moi j’ai des… Je bosse avec des développeurs, parfois c’est le cas. Et eux ça les intéresse pas d’aller voir le client finalement. Et l’important justement c’est d’aller montrer à ce client et d’aller le rassurer en lui répondant du tac au tac. On a ça. Et puis de l’autre côté, au moment de la compet’, de plus en plus on a des SI qui ont besoin finalement de réponses tout de suite et qui vont poser des questions super-techniques, et parfois super-précises. J’dis n’importe quoi, mais quand on est dans un contexte de problématique de responsive web design, il y a quelqu’un qui va arriver comme ça, avec des questions de but en blanc dans la compétition en disant : « Alors comment est-ce que vous allez faire ? Est-ce que vous allez gérer vos typos en em ou en pourcentages ? ». Concrètement, les consultants qu’on avait avant – et c’est là la différence entre l’expérience et l’expertise – c’est qu’on essaie finalement d’apporter cette expertise technique à ces moments-là du projet, pour finalement répondre du tac au tac au client. Et finalement ça rassure quoi. J’sais pas si ça répond ?... [pause assez longue]</p>

<p>La présidente de séance : voilà, c’est fini.</p>

<p>Florian : voilà. C’est magique ! [rire soulagé]</p>

