    <div class="talk-description"><p>Web Evangelist, Developer Advocate, Creative Technologist : Des métiers dont on ignorait encore tout en agence il y a peu.</p>

<p>Ces nouveaux profils ont dû se créer entre équipes de développement front grandissantes et clients de plus en plus technophiles et exigeants.<br />
Ceux-ci n'arrivent plus simplement avec un besoin mais avec une véritable liste de course technique (ex: « Je voudrais un site en HTML5, Responsive Design, avec des animations CSS, qui est optimisé pour iPad ET IE6… » ) qu'il faut parfois démystifier et rationaliser.</p>

<p>Nos clients ont maintenant besoin de comprendre et les problématiques front-end doivent de plus en plus être anticipées et précisées.</p>

<p>Cette présentation a pour objectif de montrer combien ce rôle d'intermédiaire et d'accompagnement facilite la gestion de projets web et devient indispensable dans nos structures, mais aussi comment cela peut se traduire au quotidien.</p></div>
