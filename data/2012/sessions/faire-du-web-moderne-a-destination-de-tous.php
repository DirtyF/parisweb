IS : oui, rebonjour.<br />
C’est juste encore par rapport aux histoires de IE6 dans les grands comptes et avec les navigateurs etc.<br />
Dans les grands comptes de plus en plus je vois qu’ils ont effectivement, contrairement à ce que disait Stéphane, un parc qui est en général pas plus vieux de trois ans.<br />
