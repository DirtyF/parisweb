    <div class="talk-description"><p>Les navigateurs modernes ont des possibilités décuplées par rapport à seulement quelques années auparavant : graphiques bien sûr avec CSS3, du balisage dans HTML5, des APIs, des fonctionnalités Javascript. Une certaine monoculture dans le web mobile amène aussi certains développeurs à ne garder qu'une compatibilité envers quelques moteurs récents.</p>

<p>Dans cette présentation, Julien Wajsberg vous montrera qu'il est réellement facile de rester compatible dans une certaine mesure, et vouss présentera ses différentes astuces pour pouvoir utiliser les nouvelles fonctions tout en gardant le site ou l'application web utilisable sur des navigateurs plus anciens : duplication de fonctions (ou pas), mode dégradé ou rehaussé, Modernizr, et d'autres choses. Et si on pouvait non seulement obtenir <strong>un web plus compatible, mais aussi mieux préparé pour le futur ?</strong></p></div>
