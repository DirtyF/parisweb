		<header><h2>Transcription</h2></header>
		<p>Faire du web moderne à destination de tous<br />
Par Julien Wajsberg<br />
 <br />
Aujourd'hui je vais essayer d'expliquer comment on fait pour développer à la fois avec les trucs que vous avez vus ce matin, par Daniel, par Luz, et mais tout en faisant des sites qui fonctionnent toujours avec peut-être des navigateurs anciens. Ça c'est le premier point.</p>

<p>Et le second point que je vais essayer d'expliquer c'est comment on fait pour... Éviter en tout cas de se focaliser sur un seul moteur. Ce qu'on voit un peu trop souvent dans le monde mobile.</p>

<p>Voilà. Donc moi je suis Julien Wajsberg, Je travaille chez Orange depuis huit ans, et pour encore deux semaines. [rires]</p>

<p>Ça fait environ deux ans qu'on travaille sur un gros projet de bureau en Web. Donc on a mis en place pas mal de choses, on a mis en application pas mal de trucs comme ça, parce qu'on veut supporter Internet Explorer 7. Et je voudrais essayer de partager tout ça avec vous.<br />
Après c'est juste une expérience personnelle et je suis très preneur de trucs que vous pouvez avoir dans la salle.</p>

<p>Donc, d'abord je vais essayer de poser le problème.<br />
Dire un petit peu quels sont tous les défis qu'on a à développer pour du multi-device, sur du multi-écran. Et ensuite, voilà, vous expliquez un petit peu du point de vue technique ce que moi j'ai pu mettre en place. Alors ça vous avez peut-être déjà dû voir, bon la qualité est pourrie mais on voit quand même le principe.</p>

<p>C'est une boîte qui s'appelle Animoca là qui montre un petit peu tous les devices qu'ils utilisent pour tester. Je vais trop vite ? C'est ça ? Ça sature ? Faut mettre moins fort alors ! C'est moi qui parle fort.</p>

<p>On voit qu'il y a vraiment beaucoup beaucoup de devices. C'est un des premiers problèmes en fait.<br />
Beaucoup de devices, beaucoup de résolutions. On sait que sur PC par exemple ça peut aller du 1024 par 768 au 1600 fois je sais plus combien, voire plus pour ceux qui ont des gros iMacs. Il y a des problèmes de densité avec tout ce qui est écran haute résolution type Retina. Dans le monde des tablettes c'est similaire. On part des tablettes 7 pouces, on peut peut-être même mettre le note 5 pouces là-dedans, donc des résolutions de type 800X600. Et ça monte jusqu'à 1024, et sans doute plus à l'avenir.</p>

<p>Et dans le monde mobile, on a quelque chose d'à peu près standard, on va dire, chez Apple. Un peu moins chez Androïd, et puis je parle pas des autres.</p>

<p>C'est un premier problème que l'on peut résoudre avec responsive design comme on l'a vu un peu plus tôt dans une conférence déjà. C'est pas le but d'aujourd'hui. Je ne vais pas parler de ça. Parce que je sais pas trop, en fait.</p>

<p>Deuxième problème : un problème lié aux entrées utilisateurs.<br />
Il y a vraiment une multiplicité de moyens d'entrer les informations. Sur les PC de bureau on a les classiques clavier/souris, et on commence à voir arriver des écrans touch. Sur les tablettes évidemment touch. Du stylet sur certaines tablettes. Le clavier virtuel que l'on aime pas trop. Et enfin sur les mobiles on a un peu plein de trucs. Alors sur les anciens mobiles on avait le stylet. On commence à le revoir aujourd'hui. On a la molette sur certains, c'est plutôt sympa aussi. Enfin voilà. Je vais pas en parler non plus.</p>

<p>Troisième défi qu'on peut avoir c'est tout ce qui concerne les contextes. Les contextes au pluriel.<br />
Alors là je vous en ai mis... Il y a deux types de contexte, je dirais. </p>

<p>Il y a le contexte lié à la localisation. Chez soi, on est tranquillement installé sur son canapé.<br />
On regarde la télé. On regarde la télé et en même temps pianote sur son mobile ou sur sa tablette.<br />
Il y a peut-être un problème d'attention peut-être ?<br />
Quand on est au bureau, chez soi, sur son ordi normal tout va très bien on est très concentré. </p>

<p>On peut être dans un magasin, on peut avoir des infos sur un produit. Dans ce cas là on va être au fin fond du magasin, on va avoir un problème de connectivité. Ça va aller très doucement. On risque de ne pas avoir toutes les ressources d’ailleurs, on peut avoir qu'une partie de la page s'afficher.</p>

<p>Dans le métro, dans le train, alors dans le train en a des problèmes de vitesse. On sait que dans un TGV, vous avez dû faire l'expérience, ça marche très très mal le mobile. Dans le métro à Paris, pour ceux qui sont pas parisiens, il faut savoir qu'il y a pas de 3G. Il y a que du Edge. Je sais pas si il y a quelqu'un de la RATP ici ?</p>

<p>Ouais, il y en a quelques-uns. [Rires]<br />
Ah, je l’ai repéré, il y en a 1.</p>

<p>Voilà donc en fait il y a quand même quelques millions de personnes qui voyagent dans le métro chaque jour et ces millions de personnes ben utilisent quand même Internet. Ils essayent, et ça marche pas forcément très bien sur des sites qui sont lourds. Voilà.</p>

<p>Après, il peut y avoir des problèmes d'environnement. Dans un parc avec du soleil on a sa tablette avec ces supers écrans brillants qui semblent si jolis dans le magasin. Mais qui sont si peu pratiques une fois qu'on est en extérieur.</p>

<p>Enfin il y a tous ces problèmes de contextes.<br />
Mais je vais pas en parler non plus aujourd'hui.</p>

<p>Ah oui c’est un point que j'allais oublier Luke Wroblewski c’est quelqu'un que j'aime beaucoup avec un site génial sur lukew.com. Qui a écrit Mobile First chez Bookapart, bref lui il a un peu synthétisé une étude qui dit en gros : les tablettes c'est chez soi. Chez soi ça peut vouloir dire en vacances. <br />
Et les Smartphones c'est plutôt partout en mobilité, on a toujours sur soi. </p>

<p>Bon, c'est un peu une parenthèse. Je trouvais intéressant.</p>

<p>Et enfin, ce qui nous intéresse aujourd'hui c'est les différences dans les OS et dans les navigateurs. Alors, sur iOS c'est assez simple. Il y a ça sur les mobiles. Il y a quand même Opera Mini. C'est peut-être pas si simple que ça finalement. Ouais, c'est pas un vrai Chrome. C'est un Chrome avec le moteur Webkit de Safari et avec je crois un JavaScript qui est pas optimisé donc… Bon c'est un faux Chrome quoi ! Chrome, on peut accéder à ses données, sur son cloud Google. Voilà, c'est bien pour Google.<br />
Sur Android, on a un peu plus de choix aujourd'hui. Bon on retrouve Androïd Browser évidemment.<br />
Là on a le vrai Chrome qui arrive.</p>

<p>On a les deux Opera : Opera mobile et Mini qui sont complètement différents. Firefox qui arrive évidemment. Voilà. Et puis sur la plate-forme Windows on a IE, IE puis IE.<br />
Même si je pense qu'il y aura du Opera Mini aussi.<br />
Je crois qu'il y a un Firefox qui est en train d'arriver sur Windows 8 aussi.<br />
Donc en fait voilà les 5 gros défis auxquels on est confronté.<br />
Aujourd’hui on va parler surtout du dernier.<br />
Donc alors rappelons-nous un petit peu, rappelons-nous en 2001 Internet Explorer 6.<br />
En 2004 Firefox 1.<br />
Alors il avait déjà des trucs avant mais ça n'avait pas beaucoup de succès.<br />
2005, IE7 arrive.<br />
Et en 2006, il sort vraiment.<br />
Coïncidence, 2006, première édition de Paris Web. [Rires]<br />
En fait, on a appris qu'en fait, en développant pour une seule plateforme, en fait, on la fait stagner.<br />
C’est lié à la faute des développeurs avant tout.<br />
Ils ont pas développé pour tout.<br />
Bon ça c'est quelque chose que l'on ressasse depuis assez souvent.<br />
Mais c'est quelque chose qu'il faut évidemment qu'il faut remettre en regard aujourd'hui, en 2012.<br />
Alors en 2012, on des navigateurs modernes partout.<br />
Ils nous entourent.<br />
Je les ai tous mis, j'ai même mis IE10.<br />
Il sort que ce mois-ci mais…<br />
Par contre, sur mobile, on a du Webkit.<br />
C'est omniprésent.<br />
Alors WebKit c'est évidemment Android, Chrome, Safari mobile, c'est le Kindle.<br />
Mais la vérité est un peu plus subtile, puisqu'on a Opera Mini, on a Firefox qui commence à arriver, on n'a pas surtout la plate-forme Windows dont j'ai parlée.<br />
Donc Windows phone Seven depuis un an, et puis Windows 8 qui arrive ce mois-ci.<br />
On peut imaginer que c'est Noël cette année, il y aura peut-être des Windows dans la nature.<br />
Donc faudra s'en occuper.<br />
Tous les sites qu'on a faits depuis quelques années et qui marchent pour iPhone, donc iOS, et que pour iPhone, on va mettre que du Webkit.<br />
Ben, ils vont être pétés.<br />
Voilà ! C'est juste la vérité quoi.<br />
On a quand même des navigateurs encore anciens justement.<br />
Suivant un petit peu les stats, on voit qu'il y a de 15 à 30 % finalement de parts de marché pour IE7 et 8.<br />
IE6 dans certains pays, la Chine par exemple, il y a, on m'a dit 40 % hier, d'IE6.<br />
Ça va être important de cibler, ben de cibler justement ses utilisateurs pour choisir quel navigateur on veut supporter.<br />
C'est vraiment la base.<br />
Faut pas dire : je fais que ça parce que c'est plus facile.<br />
Faut dire je fais que ça parce que j'ai réfléchi un petit peu.<br />
Donc là j'ai piqué un peu ça façon Courrier International.<br />
Nous devons nous assurer de conserver la diversité.<br />
La diversité est bénéfique pour le Web.<br />
Voilà donc pour poser le problème un petit peu. Alors maintenant je voudrais essayer d'expliquer qu'est-ce qu'on a de nouveau.<br />
En gros, on a plein de trucs nouveaux depuis je dirais 4, 5 ans.<br />
On a des nouveautés en HTML5, on a des nouveautés en CSS3, on a des nouveautés en JavaScript.<br />
Alors tout d'abord en HTML5 :<br />
Oui, je vous ai pas dit, le début de cette présentation c'est la partie jolie.<br />
Là j’ai la partie avec mode texte maintenant.<br />
Mais j'ai quand même mis du orange.<br />
D'abord HTML5, il y a des nouvelles balises.<br />
Donc ça c'est quelque chose je pense qu'on a déjà pas mal entendu.</p>

<p>J'ai une opinion assez tranchée là-dessus :<br />
Je trouve qu'elles servent pas à grand-chose.<br />
Donc, il y a plusieurs possibilités pour les utiliser. On peut utiliser un script.<br />
Avec les dangers…<br />
Qui fonctionne bien, mais avec les dangers qu'on a qui font que si on arrive pas à télécharger ça marchera pas.<br />
Il y a une technique un peu Sioux et qui consiste à utiliser les préfixes XML.<br />
A réserver aux Daniel Glazman.<br />
On peut utiliser à la fois ces nouvelles balises section, articles etc. et des div qui ont le même nom.<br />
Moi je vois pas l'intérêt.<br />
Je trouve qu'il vaut mieux du coup utiliser directement les divs avec des classes particulières.<br />
Alors par contre on peut quand même préparer l'avenir.<br />
On peut dire que dans notre CSS, quand on va styler, on va styler à la fois le div .article par exemple, et article.<br />
Comme ça, dans le futur, imaginons que dans un futur plus ou moins proche, il n'y ait plus d'IE7 et IE8, <br />
Qui sont à moins de 1 % de parts de marché,<br />
Voilà, on peut reprendre le site.<br />
Tout ce qui était div.article, on le remplace par article.<br />
Pas besoin de toucher au CSS.<br />
Parce qu'on l’a prévu pour l'avenir.<br />
C'est pas difficile à faire, je pense.<br />
Donc c'est juste mon opinion.<br />
Je suis prêt à entendre les opinions contraires.<br />
D'un point de vue multimédia, il y a pas mal de choses aussi, donc audio, vidéo, canvas.<br />
Là j'ai un peu expliqué comment on peut les utiliser de manière multi-navigateur.<br />
Donc, vidéo et audio on peut utiliser un fallback Flash.<br />
Alors, il y a des choses qui existent, des librairie qui permet de s'occuper de tout ça pour nous en fait.<br />
C'est ce que je vais voir après.<br />
C'est ce qu'on appelle un wrapper en gros.<br />
Et on va les utiliser.<br />
On va utiliser audio ou video si c'est là.<br />
Et pour fallbacker on va faire du flash autrement.<br />
Canvas, c'est un peu similaire, il y a un fallback en JavaScript qui existe, qui marche pour Internet Explorer.<br />
Attention ! C'est très lent ceci dit !<br />
Donc faut pas juste dire : c'est bon je fais du canvas super top,<br />
et je mettrai ça, et puis de toute façon ça va marcher.<br />
Non ! Ça rame vraiment.<br />
Je vais en parler de ça aussi après.<br />
Il faut pas oublier de mettre le fallback texte aussi entre canvas et /canvas.<br />
Souvent il y a un texte pourri qui est :<br />
Votre navigateur supporte pas canvas.<br />
Ça fait une belle jambe pour ceux qui… Pour les lecteurs d'écran.<br />
Et les revues d'écran récentes, de 2012, savent le lire.<br />
C'était pas le cas auparavant.<br />
On a également de nouveaux attributs notamment sur formulaire.<br />
"Tel", "number", "url", c'est super intéressant,<br />
C’est à utiliser puisque les navigateurs qui supportent pas mettront un champ texte,<br />
Et les navigateurs qui le supportent, notamment sur mobile, mettront un clavier adapté.<br />
Je sais pas, qui connaît déjà ça en fait ?<br />
Ouais, pas mal de monde en fait !<br />
Ce que vous savez peut-être pas c’est que les inputs de type color et date, même si c'est implémenté, ça pose des gros problèmes d'accessibilité, et notamment en navigation clavier.<br />
C'est le monsieur là, qui a fait des tests, j'ai pas testé moi.<br />
Mais vous pouvez aller le voir si vous voulez.<br />
Ça pose des gros problèmes d'accessibilité en navigation clavier.<br />
Ça pose des gros problèmes aussi je pense en revue d'écran.<br />
Même si ça s'améliore encore une fois, tout le monde n'a pas la technologie la plus récente, parce que ça coûte très cher.<br />
Attributs de validation, bon, je vais passer, je pense que c'est assez facile.<br />
Et les attributs data, c'est enfin une manière standard de spécifier les attributs.<br />
Auparavant on n'en mettait déjà, mais on mettait des trucs propriétaires, on va dire.<br />
Avec ça, on peut l'utiliser dès maintenant.<br />
Ensuite sur les APIs :<br />
Il y a beaucoup d'API très récentes : Web Socket, Poke Message etc.<br />
Bien sûr c'est pas présent dans les vieux navigateurs.<br />
Ensuite, c’est présent pas sur tous les navigateurs modernes,<br />
Et des fois quand c'est présent, c'est pas compatible.<br />
Un exemple très clair c'est IndexDB.<br />
Vous connaissez peut-être ?<br />
C'est une manière de… IndexDB, base de données, voilà.<br />
Ça permet de faire une base de données côté navigateur.<br />
Il y a des choses qui existaient avant, comme Web SQL database. Ça a été rendu obsolète.<br />
IndexDB, la manière standard de faire aujourd'hui.<br />
Mais voilà, c'est pas implémenté partout.<br />
