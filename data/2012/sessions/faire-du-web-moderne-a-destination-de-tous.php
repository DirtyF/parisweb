IS : moi je voulais revenir un peu sur un projet de cette taille, avec le nombre d’équipes, le nombre de temps passé dessus.<br />
Est-ce que dans la conception, dès la conception, à partir de quelle étape vous prenez en compte cette amélioration progressive ou celle de…<br />
Pour faire en sorte de dire cette fonctionnalité soit on la prend pas, soit on la prend mais ça implique tel choix en termes de performances, tel choix en termes de fichiers en plus à télécharger, en termes de façon de l’appréhender soir on…<br />
Est-ce que cette étape-là est processisée chez vous ?<br />
Est-ce que vous avez un moyen de l’organiser ?<br />
Julien : chez nous, c’est encore un peu… En fait on n’est pas une agence de développement Web, on fait des téléphones, et des réseaux.<br />
Chez nous c’est encore vraiment au niveau de l’artisanat, je dirais.<br />
Ça commence un petit peu à se professionnaliser.<br />
On a eu récemment un petit Paris Web interne, on va dire.<br />
Donc ça commence.<br />
Et du coup, ben c’est un peu de l’artisanat, et c’est un petit peu chacun, chaque développeur qui décide on va dire peut-être au sein de l’équipe.<br />
Alors nous on a fait le choix qu’on supportait IE7.<br />
Comme c’est moi qui ai commencé à coder j’ai fait tout de suite des trucs pour que ce soit facile à supporter.<br />
Les polyfills pour les foreach les maps etc.<br />
En tout cas chez nous, qui ne sommes pas une agence Web encore une fois, c’est pour l’instant à ce niveau-là.<br />
C’est dommage ! Mais je pense que ça va changer.<br />
