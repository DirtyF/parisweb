IS : Bonjour. Anthony.<br />
Pour rebondir sur ce que disait Mathieu sur les outils intéressants.<br />
Il y a aussi des ralentisseurs de connexion sur les systèmes d’exploitation, donc pour simuler du 3G, du Edge, des paquets perdus des choses comme ça.<br />
