IS : oui oui. Mais c’est plus sympa de l’avoir sur ta machine en développement journalier.<br />
Vous avez des tests automatisés pour tous les navigateurs ? Pour vérifier que ça casse pas ?<br />
Julien : quand tu dis nous, tu dis nous Oranges pour le portail par exemple ?<br />
IS : j’étais pas là au début, donc je savais pas que tu avais des problèmes. [Sourires]<br />
Mais c’est une fausse question, en fait. Je disais il faut des tests automatisés. [Rires]<br />
Quand on utilise des choses pas standards, pas encore standards, quand on utilise des préfixes, ça casse très souvent.<br />
Quand la version non préfixée arrive dans une version de navigateur, comme par hasard ils ont cassé la version préfixée.<br />
Enfin il y a plein de choses qui peuvent casser. Il faut des tests automatisés.<br />
Julien : chez Chrome.<br />
IS : non chez tout le monde ! Mais oui évidemment Google… [Rires]<br />
Julien : les tests automatisés c’est bien, mais ces chiant, faut dire qui est. c’est pas toujours fait.<br />
Mais c’est bien si on en fait. C’est clair.<br />
Il y a des outils, et faut peut-être prendre le temps de le faire. <br />
[NDT : Selenium] ou des trucs comme Kasper peut-être ?<br />
[Un intervenant qui parle]<br />
Julien : il dit que Kasper c’est Webkit donc pour faire des tests automatiques avec Kasper c’est dommage.<br />
