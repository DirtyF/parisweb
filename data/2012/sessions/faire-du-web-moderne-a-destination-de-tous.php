J’aurais tendance à dire que la question de Stéphane contient en elle-même sa propre réponse.<br />
À savoir : la spec du W3C ne précise pas le sens sémantique des différentes balises. [Autre intervenant : c’était le cas il y a quelques temps].<br />
Bah oui mais c’est plus le cas !<br />
Et du coup, en fait, si on les utilise, on les utilise sans savoir vraiment ce qu’on doit mettre dedans.<br />
Et du coup, ça n’a pas trop de sens.<br />
En fait, la manière dont ont été choisie le faite de rajouter ces balises sémantiques, c’est en analysant sur un panel de pages Web existant dans le monde quelles étaient le nom des classes et des identifiants des classes les plus utilisés et on a créé ces balises en se disant :<br />
Autant créer des balises représentant ce que les développeurs dans les pages… Sur lesquels les développeurs ont cherché à mettre de la sémantique, puisqu’ils utilisaient ces balises.<br />
Sauf que, la spec ne précisant pas la sémantique de ces balises, les développeurs ne les utilisent pas en se disant :<br />
si je les utilise, que je mets une sémantique dessus, c’est pas dit du tout que la sémantique qui va être faite par quelque chose qui va les analyser automatiquement par ailleurs sera la même que la sémantique que je vais mettre moi.<br />
Donc, il vaut mieux ne pas les utiliser pour pas qu’il y ait un risque de mésentente sémantique.<br />
Donc voilà.<br />
Julien : en fait, faut pas me faire dire ce que j’ai pas dit.<br />
Je trouve que c’est très bien.… Moi je les trouve très bien.<br />
En fait, le vrai intérêt ça va être avec la hiérarchisation des titres.<br />
C’est-à-dire qu’on peut mettre un… Si on met une section avec h1 dedans, et qu’il y a un h1 au-dessus du bac automatiquement, le H1 qui est dans la section il va être décalé par rapport au… Enfin ça va être comme un h2.<br />
Ça c’est un vrai intérêt mais le problème c’est qu’en pratique aujourd’hui, il y a trop d’inconvénients, parce qu’il y a trop de navigateurs qui supportent pas.<br />
Et ça va faire n’importe quoi dans ces navigateurs-là.<br />
Et surtout que ces vieux navigateurs c’est des vieux Jaws qui se font plus et qui auront pas la bonne structure.<br />
Pour l’instant, je dis pas qu’il faut pas les utiliser à l’avenir.<br />
Mais aujourd’hui, en 2012, je pense moi personnellement que c’est pas une bonne idée.<br />
Question 4<br />
IS : je voulais juste faire un petit complément de réponse par rapport à ce que tu disais Stéphane.<br />
On a appris aussi un truc qu’on sait pas forcément : pourquoi les grands comptes ne migrent pas leur Navigateur ?<br />
Il y a beaucoup de grands comptes, notamment dans le secteur bancaire, qui avec IE6 ont fait énormément de développements qui leur ont coûté des centaines de milliers d’euros, autour des ActiveX ce genre de choses.<br />
Et qui ne peuvent pas en faite passer à des navigateurs plus récents parce que ça leur coûterait des milliers de milliers d’euros de refaire leurs applications propriétaires.<br />
[Autre intervenant : et qu’ils ont pas le code côté serveur parce que, pour faire le développement il faut qu’on récupère le code.]<br />
Question 5 :<br />
IS : je trouve, dans le même esprit, vous parlez de dégradation gracieuse.<br />
Moi, enfin, je trouve que c’est quelque chose qu’il faut changer.<br />
Il va falloir parler d’amélioration progressive à son client.<br />
C’est vachement important parce que c’est vraiment… C’est la manière de dire les choses.<br />
C’est exactement la même chose en termes d’implémentation.<br />
Mais c’est comme ça aussi qu’on va faire évoluer les mentalités.<br />
Et c’est en arrêtant de voir le verre à moitié vide et en essayant de le prendre à moitié plein.<br />
Et alors c’est clair : ça ne changera rien en termes de technique.<br />
Mais par contre on a des clients qui sont vachement réceptifs.<br />
Moi je le vois tous les jours.<br />
J’ai des clients quand on leur parle d’amélioration progressive dise :<br />
Finalement c’est peut-être comme ça que je vais faire quelque chose de super pour quelqu’un et de normal pour un autre plutôt que l’inverse : je vais faire quelque chose de fonctionnel pour quelqu’un et d’un peu nul pour l’autre.<br />
Julien : moi ça m’intéresse vachement que tu proposes une conf’ un jour où tu expliques comment tu expliques ça aux clients.<br />
Moi clients, enfin tel que je le connais, parce que c’est moi le client en l’occurrence chez Orange.<br />
Si on dit un client : ouais on veut d’abord un truc pourri, puis après peut-être que pour les autres on fera un truc joli, il va pas te prendre.<br />
Du coup, ouais, c’est ma vision à moi et je me dis qu’il faut une conf’ pour que tu expliques ça.<br />
IS : OK mais ça c’est peut-être un problème d’ergonomie aussi. Peut-être qu’il faut revoir le Web d’une manière différente aussi.<br />
Et je pense qu’on peut arriver à des choses supers riches dans le numérique etc. sans pour autant faire l’interface hyper lourdingue.<br />
On l’a bien vu avec des choses qui ont disparu comme drag & drop etc.<br />
Et finalement c’est pas un mal pour les utilisateurs.<br />
C’est qu’un moment parfois faut pas chercher à faire du waouw partout.<br />
Et peut-être qu’il faut revenir à quelque chose de plus simple.<br />
On est revenu là-dessus avec le mobile, le responsive etc.<br />
Et je pense que c’est quand même un truc qui est en train de se passer.<br />
C’est qu’aujourd’hui on se rende compte que si on veut faire sites multi-device pour tous les clients, pour les mobiles et pour tout le monde,<br />
On peut réussir à faire parce que ouais !<br />
Parce qu’on va peut-être s’adapter au mobile.<br />
Finalement peut-être que la clé c’est le mobile !<br />
Et le mobile, ben finalement quand on fait du mobile, on peut faire du IE6 quoi.<br />
Et moi j’aime à dire que… Non mais…<br />
Ce que je veux dire c’est que moi j’aime à dire que en dehors des animations et tout ça qui sont de l’ordre du superflu,<br />
Il y a beaucoup de choses que l’on peut faire si on pense vraiment à utilisateur comme tu le disais, et plus forcément que à l’interface.<br />
Mais vraiment à l’interface pour l’utilisateur.<br />
Julien : on peut l’applaudir [applaudissements]<br />
Question 6<br />
IS : salut, Mathieu, couteau suisse du Web.<br />
J’ai une remarque une question un peu ouverte.<br />
Ma remarque c’est sur le fait de tester avec du vieux matos pour voir ce que ça donne pour les utilisateurs qui n’ont pas forcément un quadri-processeur avec 28 Giga de ram.<br />
Julien : et dans le métro.<br />
IS : ou dans le métro. Il y a plein de petites astuces très bêtes, sans changer votre matos pour pouvoir tester dans ces conditions-là. Par exemple, l’année dernière c’était plutôt sur du back-end, il y a quelqu’un qui m’a suggéré pour tester avec une base de données chargée de mettre la base de données sur un disque dur USB.<br />
Vous pouvez faire la même chose avec votre cache de navigateur en fait.<br />
Le configurer pour le mettre sur un disque dur USB, vous reloadez votre page et vous voyez à quel point c’est lent.<br />
Ça peut simuler des trucs comme ça.<br />
Vous pouvez prendre PhotoShop ou un GIMP et le lancer en arrière-plan en lui demandant de mettre un blur sur un document de 2000×2000.<br />
Un truc comme ça. Et pendant ce temps vous testez votre site.<br />
Et comme ça vous pouvez vraiment vous rendre compte de ce que ça donne avec un matos de merde, sans avoir le matos de merde en question.<br />
Voilà, y’a plein de petites astuces comme ça sympa.<br />
Ou lancer une vidéo 1080 p derrière pendant que vous relancez vos supers animations sur votre site Web.<br />
Vous allez voir les animations sur site Web vont être nettement moins fluides.<br />
Et voilà, vous pouvez vraiment vous rendre compte de ce que ça donne chez tout le monde qui n’a pas votre matos.<br />
Donc ça c’était voilà. petite remarque façon couteau suisse.<br />
Sinon je voulais savoir.<br />
Parce qu’on discutait de grands comptes qui pourront jamais, avant des années, avoir des versions récentes etc.<br />
est-ce que tu avais une opinion sur le fait de dire sur le site aux utilisateurs : vous avez un navigateur tout pourri, donc la page Web va pas forcément bien s’afficher ?<br />
Sans forcément l’engueuler, sans forcément le pousser à le mettre à jour, juste au moins le prévenir et lui dire :<br />
Peut-être que tu pourrais faire pression sur ta hiérarchie, on sait jamais, ça finira peut-être par marcher.<br />
Ou un truc comme ça quoi voilà.<br />
Julien : enfin la hiérarchie elle-même va tomber dessus. Peut-être.<br />
IS : peut-être aussi oui.<br />
Julien : je réponds rapidement si tu veux.<br />
Je pense que c’est bien ce que tu dis. De faire ça voilà. J’adore tout ce que tu fais. [Rires]<br />
 <br />
Sérieusement, par exemple quand Github a fait le choix de ne plus supporter IE7, je crois, récemment.<br />
Sauf que tu vas sur Github avec IE7, y a rien qui s’affiche comme quoi il ne supporte plus.<br />
[Une vélotypiste demande d’épeler].<br />
Julien : Github : g i t h u b.<br />
Et on a juste une page toute pétée.<br />
Sans explication.<br />
Donc je trouve ça… C’est vraiment pas respectueux envers les utilisateurs qui vont dessus.<br />
