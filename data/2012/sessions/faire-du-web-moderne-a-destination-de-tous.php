IS : juste pour rebondir sur cette histoire de préfixes.<br />
On a écrit un article sur OpenWeb sur le sujet. On a fait tester par des gens qui avaient IE10 en version pre-release.<br />
Et sur la version quasiment finale.<br />
On s’est aperçu qu’il fallait quand même garder les préfixes même si Microsoft a annoncé les enlever sur la dernière.<br />
Donc vaut mieux se les trimbaler, c’est plus sûr.<br />
Julien : mais tu parles des préfixes MS là ?<br />
IS : oui préfixes MS bien sûr.<br />
