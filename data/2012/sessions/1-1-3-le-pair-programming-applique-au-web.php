    <div class="talk-description"><p>Depuis la nuit des temps, de Laurel et Hardy à Olive et Tom en passant par Stone et Charden, les hommes et les femmes ont compris que l'<strong>union faisait la force</strong>. Pour autant, le sujet reste relativement tabou dans l'informatique et les métiers du web en particulier, et bon nombre d'entre nous n'osent pas franchir le pas et continuent de travailler chacun dans leur coin. </p>

