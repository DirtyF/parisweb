    <div class="talk-description"><p>Dans sa nouvelle “When sysadmins ruled the earth”, Cory Doctorow évoque une fin du monde plausible, en fait une presque fin du monde, mystérieuse qui ne laisse vivants que les sysadmins, seuls rescapés dans leurs datacenters hermétiques. Tout se passe alors en ligne. La vie ne peut plus être que connectée, le réseau étant seul moyen de communication, de partage, tandis que le monde revient peu à peu à la réalité, à la vie après les ravages d'un virus inconnu. Autre vision, autres temps, Barjavel quant à lui annihile la technologie pour un monde seul tourné vers la nature. Deux extrêmes : réseau or not réseau, technologie or not technologie.</p>

<p>C'est un monde mixte qui nous attend en fait, la fin du monde de Cory Doctorow, celui du réseau à l'ancienne, expérience vécue uniquement à travers l'écran, et la venue du monde de Barjavel mais où le réseau et la technologie se sont faits invisibles. Nos échanges au quotidien, notre mobilité le prouvent. Aujourd'hui, c'est du réel au réel. Le numérique s'efface pour ne laisser que les actes réels s'interconnecter avec des personnes, des lieux, des idées, des ressources, des connaissances. Nous numérisons notre vie pour en tirer le meilleur parti, pour construire des prothèses qui nous rendent mutants ici et ailleurs, présent ici et là, dans le monde entier, sur le réseau par notre double numérique. Le concept de « prothèses » est connu depuis longtemps mais aujourd'hui elles s'effacent, elles disparaissent peu à peu, tandis que le réseau est en, vit en nous et se développe tout autour de nous. C'est cela qui marque la fin d'un monde et l'arrivée d'un nouveau.</p></div>
