	<article id="transcription-670">
		<header><h2>Transcription</h2></header>
		<p>Bonjour. Merci de votre présence. Je vous rassure : je ne vais pas parler d'un groupe de dance music des années 90... Rien à voir... Pour commencer de manière traditionnelle, trente secondes de me, myself and I : je suis historien médiéviste à la base, enseignant en Sciences de l'Information, libriste, développeur, ce qui explique qu'un historien peut venir vous causer nouvelles technologies et web.</p>

<p>Quand j'ai vu le thème "fin du monde", j'ai pris peur parce c'était grave. Pour un historien, la fin du monde, c'est plus de boulot, plus rien à raconter. Il fallait faire quelque chose et donc pourquoi pas venir théoriser un concept sur lequel je travaille pas mal, parfois avec mes étudiants.</p>

<p>Bref, c'est la fin du monde, mais on peut opposer deux fins du monde et je voulais commencer avec une première vision et cette citation de Cory Doctorow : "We are the custodians of a deathless, monstrous, wonderful machine, one with the potential to rebuild a better world." (Cory Doctorow - When Sysadmins Ruled the Earth)</p>

<p>Je ne sais pas si vous l'avez lue. Je vais vous la raconter rapidement. En gros, un virus, enfin plusieurs virus bactériologiques et informatiques sont en train de détruire le monde. On a l'impression que toutes les organisations terroristes sont en train de se mettre sur la g... le même jour. Tout est détruit et les seuls survivants sont les sysadmins, protégés dans leurs datacenters hérmétiques, et qui ont assisté indirectement à l'effondrement du monde, des structures et surtout à la mort de leurs familles. Peu à peu, ils se relèvent, se réorganisent et l'un d'entre eux dit cette phrase superbe.</p>

<p>Les sysamdins ont donc entre les mains cette machine immortelle, monstrueuse et merveilleuse qu'est Internet et qui, surtout, peut reconstruire un meilleur monde. Personnellement face au réseau, je me sens tout petit et en même temps hyper important. Peut-être par rapport à un souvenir d'enfance : la première fois que j'ai vu un ordinateur, j'ai eu de la chance, ce n'était pas un ordinateur mais des ordinateurs. C'était dans une salle de conférences, chaque conférencier avait son ordinateur, ils étaient en simili-réseau car on ne parlait pas encore d'internet à l'époque. C'était avant le web.</p>

<p>Et puis face à cela, il y a l'autre fin du monde, celle à laquelle on pense le plus souvent, façon Barjavel et Ravages. Plus d'électricité, plus de réseau, plus de technologie. On rase tout. Imaginez si demain subitement tous vos appareils devenaient inutilisables.</p>

<p>On se situe en fait entre deux extrêmes.</p>

<p>En fait, on vit des presque fins du monde. Je ne vous refais pas le topo pour le cinéma, la musique, Hadopi, piratage et compagnie. Même chose pour le livre numérique qui bouleverse la chaîne du livre. Idem pour la télé. Un récent sondage montrait que pour 62% des internautes, la télévision c'était internet donc Youtube, Dailymotion, etc. Pareil dans l'industrie avec l'émergence des Hacklab, Fablab, Reprap... Enfin, dans la politique : Wikileaks et le mouvement Anonymous ont quand même constitué des sacrés bouleversements tandis que des gouvernements, des collectivités freinent des quatre fers ou foncent avec joie dans l'open data.</p>

<p>Bref, il y a des grands bouleversements partout et honnêtement je suis loin de m'en attrister car je pense sincèrement que l'on vit un nouvel humanisme, une nouvelle renaissance.</p>

<p>L'autre grand bouleversement, c'est la mobilité. Je n'ai pas choisi la photo au hasard. J'avais un post de blog à terminer et envoyer. Un petit thé, mon carnet, mon mobile, tout cela entre deux manèges à Disneyland Paris... Les tiers-lieux ont pris une grande importance, que ce soit pour le travail ou au travers de nouveaux services comme Foursquare, Maps, Open Street Map, la géolocalisation en général... On voyait hier ici toutes les possibilités et l'avenir de la cartographie en ligne.</p>

<p>Je me souviens d'un petit-déjeuner que je prenais sur Paris avec Nathan Stern, le fondateur du réseau social Peuplade. On avait commencé à délirer sur différents sujets, sur différentes choses tout à fait réalisables à propos du net et, à un moment, on s'est arrêté et on a constaté que jamais on n'aurait eu toutes ces idées, jamais on n'aurait pu aller aussi loin si on avait été dans un bureau. Là, face à notre thé, nos petits pains et croissants, c'était complétement différent et la créativité était véritablement libérée, augmentée.</p>

<p>Je vais en profiter pour faire mon coming-out : je suis un lecteur de Télérama, je suis même abonné. Dans le courrier des lecteurs récemment, il y avait une lettre qui m'a fait bondir, à savoir quelqu'un qui écrivait que nous étions des associaux au travers de nos réseaux sociaux, derrière nos tablettes et smartphones en mobilité. Eh bien non ! Je comprends que cela puisse créer une rupture, choquer mais, non, nous avons l'air isolés alors que nous sommes hyperconnectés.</p>

<p>D'ailleurs, devant vous, ce n'est pas que moi... C'est moi et mes réseaux sociaux, moi et mes profils, moi et mes services, moi et mes blogs, moi et mon fil twitter, moi en tant que receveur d'informations avec mon portable, ma prothèse numérique qui vibre pour des SMS ou des mails et que j'aurais mieux fait de couper, moi en tant que livreur d'informations par mon propos mais aussi peut-être par ce que vous êtes en train de twitter en positif "cette conférence est géniale !" comme en négatif "ce mec est à ch...". C'est tout simplement moi et ma bulle numérique. Nous avons tous une bulle numérique qui se balade, qui se transporte avec nous.</p>

<p>De plus en plus, le réseau, la machinerie disparaît pour n'en plus laisser paraître que l'usage, ce que j'appelle le réel au réel car nous sommes le réseau au travers de nos prothèses numériques. Il n'y a plus qu'essentiellement des interactions entre personnes et les moyens s'effacent devant les buts. Les japonais s'activent pas mal en ce moment autour de cette question avec le concept d'O2O (online to offline) qui consiste sur un plan strictement économique à aligner le réel sur le virtuel, sur les tendances internet. Il y a des expériences plus anciennes comme le réseau social Peuplade qui visait véritablement à faire se rencontrer les gens en réel. Il y a eu des associations nées de Peuplade, des bébés Peuplade. Sur Lille, nous avons La Machine du Voisin, qui est en train d'essaimer à travers la France et qui est un mini-réseau social autour du partage de machines à laver. Ca favorise la rencontre, le petit apéro pris pendant que la machine tourne ou juste un petit complément de revenus pour certaines personnes. Récemment aussi, je rencontrais une jeune entrepreneuse qui met en place un service appelé Mamijote, qui consiste à mettre en relation des personnes sachant cuisiner et d'autres ne sachant ou ne voulant pas cuisiner. Quelques exemples en vrac... Comme le disait Howard Rheingold, nous sommes des foules intelligentes et nous numérisons nos vies pour recréer, transformer le réel.</p>

<p>Alors le Réel au Réel, l'Internet des usages, c'est aussi simple que cela et cela peut se résumer en trois points : chercher à résoudre un problème au travers d'un service, créer de la relation sociale, influer sur l'environnement personnel, afin d'aider à recréer le monde.</p>

<p>Comment on fait ? En stimulant les médias sociaux, en encourageant la participation communautaire et l'empathie. Vous savez bien que ce n'est pas parce que j'ai cliqué sur "j'aime" ou que j'indique que je participe à un événement que cela aura un véritable effet. Le "j'aime" peut être sans conséquences. Je peux très bien rester chez moi parce que je suis crevé... Il s'agit aussi de fusionner des outils et solutions existantes pour créer de nouveaux services, mais surtout de se poser la question du sens du service et de son impact sur le réel pour que la technologie ait un effet bénéfique, qu'elle puisse se partager avec le plus grand nombre. Ici nous avons de la chance. Nous sommes pour la plupart hyperconnectés, privilégiés.</p>

<p>Nous devons provoquer de l'enthousiasme vis-à-vis de ces services. C'est comme cela que des applications de geeks deviennent des applications "grand public". On sait ainsi que l'info locale est bien plus rapide sur Twitter ou Foursquare par exemple.</p>

<p>En conclusion, je vous laisse avec cette question et peut-être un début de solution : comment est-ce qu'on inverse la démarche ? En effet, nous allons chercher de l'information, nous nous inscrivons à des services. Comment fera-t-on pour que l'information, celle qui va impacter notre réel, vienne vers nous et non nous vers elle ? Le clin d'oeil, c'est qu'on est dans les locaux d'IBM et l'un des projets à mon sens les plus fous, les plus grandioses a été imaginé par un grand d'IBM, Jim Spöhrer, à travers la notion de Worldboard. En gros, c'est voir la terre, le monde comme un tableau noir sur lequel on va pouvoir taguer, placer de l'information à un niveau de granularité extrêmement fin. Je peux tagguer dans cette salle "Jeff was here". Je peux faire des graffitis numériques mais je peux aussi dans la nature tagguer une plante, placer de l'information sur cette plante parce que j'ai envie de partager cette connaissance. C'est à mon sens une grandiose transformation du réel que l'on pourrait faire. On s'en rapproche. Merci !</p>
	</article>

