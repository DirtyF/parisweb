		<header><h2>Transcription</h2></header>
		<p>Oui les places sont chères là, laissez pas des bulles au milieu, s'il vous plaît, on se décale un petit peu. Allez, soyez sympas, laissez pas vos copains assis dans l'escalier, là. Allez la rangée, compactez les bulles au milieu. Bien, on va enchaîner avec Olivia Lor qui va nous parler interaction, animation et interfaces. À toi Olivia.</p>

<p>Bonjour à tous. La vidéo que vous venez de voir qui s'appelle estime Steamboat Willie est le premier dessin animé qui a révélé Mickey Mouse au grand public, donc il a été réalisé par Walt Disney en 1928. Et comme vous le savez tous, Walt Disney est un des plus grands acteurs dans le cinéma d'animation. C'est pourquoi il y a 2 des plus grands pionniers de l'animation de Walt Disney Studios qui s'appellent Frank Thomas est Ollie Johnston qui ont écrit un livre en 1981 qui s'appelle The illusion of life. Dans ce livre, Frank Thomas et Ollie Johnston présentent les différents procédés d'animation au sein de Walt Disney Studios et dans ces procédés on en ressort 12 principes d'animation. La première s'appelle Squash ans Strech, ou élasticité. L'un des principes les plus importants en animation, parce que c'est ce qui va permettre de donner la sensation de poids et de flexibilité. Le 2ème principe s'appelle Anticipation et là pour en fait annoncer une action pour donner un côté beaucoup plus réaliste.</p>

<p>Le 3ème principe qui est le Staging ou la mise en scène, donc qui est là pour attirer l'attention de l'audience sur l'action qui va se dérouler. Le 4ème principe, Straight ahead action and pose to pose, ou toute l'action d'un coup, ou partie par partie sont 2 techniques de dessin dont la première consiste en fait à dessiner image par image et la 2ème à dessiner seulement les images clés. Le 5ème principe, Follow through and overlapping action, ou continuité du mouvement initial et chevauchement de 2 mouvements consécutifs, sont aussi 2 techniques de dessin qui prennent en compte les lois de la physique et notamment les lois de l'inertie. Donc le premier consiste en fait à indiquer que quand un corps s'arrête, ben finalement il ne s'arrête pas net, donc le corps suit le mouvement, et le 2ème c'est pour expliquer que finalement les différentes parties du corps n'avancent pas à la même vitesse.</p>

<p>Le 6ème principe qui est le Slow in et le slow out, donc l'accélération et la décélération d'un mouvement.</p>

<p>Le 7ème qui sont les trajectoires arquées.</p>

<p>Le 8ème, Secondary action, ou les détails secondaires d'un mouvement, qui consiste à mettre des détails en second plan pour mettre en avant l'action principale. Le 9ème principe qui est le timing ou la cadence qui consiste à, donc c'est pour montrer en fait finalement la rapidité de l'action, donc c'est le nombre d'images par seconde qu'on va mettre dans une action pour montrer plus de fluidité ou pas.</p>

<p>Le 10ème c'est l'Exagération qui est un principe assez utile en fait en animation parce que c'est pour rendre les objets moins, enfin qui sont réalistes moins ennuyeux et moins statiques. Le 11ème principe, Solid drawing ou réalisme du dessin, consiste à prendre en compte l'aspect tridimensionnel des objets. Et le dernier qui est Appeal ou charisme qui consiste à donner du charisme à un objet.</p>

<p>Donc pourquoi moi, Olivia, en tant que UX Designer, je vous parle des principes d'animation. Parce que finalement on reprend certains de ces principes dans les animations des interfaces, donc et ça se traduit aussi quand on regarde les définitions. Donc la définition d'une animation classique qui est toute méthode consistant à filmer image par image des dessins ou des marionnettes qui paraîtront animés sur l'écran. On retrouve cette définition dans la définition d'une animation dans une interface qui a été écrite par une chercheuse américaine qui s'appelle Cleotilde Gonzalez. Elle a écrit ça en 1996, donc finalement on reprend la définition d'une animation classique et on l'adapte à nos interfaces, donc c'est une animation qui est liée à l'action d'un utilisateur pour l'aider à percevoir un changement dans son interface et à l'aider à s'approprier la tâche qu'on lui demande.</p>

<p>Dans ces 12 principes, donc, on en reprend 6 dans les animations, dans nos interfaces qui sont le Squash and Strech, l'Anticipation, le Follow through and overlapping action, le Slow in and slow out, les Arcs et le Secondary action.</p>

<p>Alors à quoi ça sert d'appliquer les principes de dessin animé dans les animations de nos interfaces ? Est-ce qu'il y a des gens, ici dans la salle, qui ont déjà fait des animations ? Et pour qui ça s'est bien passé la première fois ?</p>

<p>Alors moi aussi, ça s'est pas très bien passé la première fois. On m'a demandé de faire une balle qui rebondit alors j'ai fait une balle qui rebondit. C'est juste un cercle qui monte et qui descend et on sent bien qu'il manque quelque chose, mais on sait pas quoi, alors c'est là que quelqu'un intervient et me dit, bon alors, et si tu rajoutes ça et que tu fais ça, machin, ça peut donner un truc plus cool, donc ça donne en effet comme ça, donc là on reprend le principe d'animation qui est le premier donc le plus important dont je vous ai parlé, le Squash and Strech et on voit bien que ça change quand même pas mal de choses, c'est pas grand-chose, mais ça fait la différence.</p>

<p>Alors comment on en est arrivés là ? Pour ça, on va faire un petit retour en arrière, un peu d'histoire.</p>

<p>On pourrait voir les prémices de l'animation commencer en 1967 dans les jeux vidéos, donc Pong, c'est le premier, un des premiers jeux vidéos qui est sorti en tout cas au grand public et comme vous pouvez le voir, il y a des animations qui sont dues à l'action d'un joueur, donc d'un utilisateur et pour l'époque, à mon avis c'était vraiment pas mal déjà, mais finalement les animations sont quand même assez rudimentaires.</p>

<p>Donc ça c'est pour le jeu vidéo, mais qu'en est-il du Web ? Donc le Web qui arrive beaucoup plus tard dans le grand public, donc en 1995 on a les applets Java qui sont sortis qui ont permis d'afficher les animations sur les sites Web.</p>

<p>Donc les animations assez lourdes et pour toutes les petites animations, on avait JavaScript, donc pour tout ce qui était les menus ou les boutons, et pour le reste on avait les GIF. Je pense que ça vous évoque pas mal de souvenirs.</p>

<p>Pour votre défense, moi aussi j'en ai filé. Donc après cette grande période de dépression graphique, en 2000, enfin en tout cas dans les années 2000, on a l'âge d'or de l'animation qui est lié à l'arrivée de Flash. Donc je pense que ça aussi on a tous connu, les sites en Flash qui ont été magnifiques avec beaucoup d'effets 3D, des effets spéciaux donc voilà, des très beaux sites.</p>

<p>En 2006, il y a eu une interface de programmation qui s'appelle Core Animation, je sais pas si beaucoup d'entre vous connaissent, mais ça a changé pas mal de choses dans l'univers de l'animation parce que finalement, ça a permis aux développeurs de développer beaucoup plus simplement les animations, en tout cas sur Mac OS, et ça a une grande influence sur ce qui va se passer par la suite parce qu'en 2007, il y a eu un grand événement. Alors est-ce que vous savez ce qui s'est passé en 2007, en tout cas dans le monde du digital, est-ce que vous avez une idée ? Voilà, donc il y a eu la sortie de l'iPhone.</p>

<p>Donc comme on le sait, Apple a placé un grand niveau d'exigence dans son design d'interface et Core Animation, ça a été un outil très pratique parce que vu qu'il permettait de développer simplement des animations, donc sur Mac OS, mais aussi IOS donc très pratique. Les développeurs étaient obligés de développer des animations de qualité puisque Apple finalement a placé la barre très haut, en tout cas au niveau des animations.</p>

<p>Donc du coup, vu que l'iPhone a très bien marché et que les ventes ont explosé, même si ce graphique date de 2008 à 2013, mais ça a finalement démocratisé les animations de grande qualité et a posé un certain standard.</p>

<p>En 2010, Steve Jobs a décidé que Flash ne serait pas compatible sur tous ses terminaux mobiles et donc du coup ça a créé le déclin de cette technologie. Mais finalement les malheurs des uns font le bonheur des autres, on s'est concentré sur d'autres technologies qui sont le HTML5 et le CSS3, donc l'avantage avec ces 2 technologies, c'est que finalement aujourd'hui on pense des animations multi devices et plus pour chaque device, avant on faisait une animation pour tablette, pour mobile, pour les sites Web, aujourd'hui c'est vraiment multi devices. Cette année, alors je sais pas si vous l'avez vu ou pas, mais Google a sorti des nouvelles spécifications d'interfaces qu'ils ont appelé Material design.</p>

<p>Et justement, ils pensent exactement de cette manière, donc ils ont revu toute leur spécification d'interface pour finalement penser multi device, et ça va finalement avec l'air du temps. Donc toutes leurs animations qu'ils ont créées sont pensées pour fonctionner sur tous les terminaux et finalement ils ont créé un langage animatique et tout un esprit autour des animations qui est vraiment la marque Google, c'est-à-dire qu'on voit, leurs animations, on voit que c'est Google qui transpire derrière. Donc pourquoi des géants comme Google ou Apple s'intéressent autant aux animations ?</p>

<p>Donc tout d'abord, à quoi ça sert ? Une animation, ça sert à rendre votre interface plus vivante et plus consistante, ça sert à lui donner une personnalité et finalement à donner une métaphore de réel. La chercheuse dont je vous parlais tout à l'heure, Cleotilde Gonzalez, a fait une étude qui démontre qu'un utilisateur comprend mieux ce qu'il voit, enfin ce qu'il voit en face de lui sur son écran si ça ressemble à des faits physiquement réels.</p>

<p>Donc là par exemple avec ce tableau de Magritte, ce qu'il a voulu dire, c'est que là, ce que vous voyez, donc c'est évidemment une pipe, mais ça ne l'est pas parce que c'est un tableau et c'est une représentation d'une pipe, mais finalement c'est beaucoup plus important parce que c'est très réaliste et donc là où je veux en venir, c'est qu'une animation, plus elle est réaliste, mieux l'utilisateur qui va se trouver en face va comprendre. 2ème chose, une animation ça sert à contextualiser l'utilisateur, donc ça sert à lui donner sa situation autant au niveau du temps et de l'espace et donc ça lui indique où il est. Donc là c'est l'application Google +, enfin encore une fois c'est Google qui font du très bon boulot en ce moment, que je trouve vraiment bien, parce qu'on sait toujours où on se trouve, les animations et les transitions sont vraiment bien situées, il y en a pas trop, il y en a pas assez, on n'est jamais perdus, bon à part quand ça rame un peu, donc plus l'utilisateur comprend son environnement, plus il sait où il est, moins il va se tromper dans son parcours et donc mieux il va vivre son expérience.</p>

<p>Donc si vous voulez pousser donc l'interactivité à son maximum, ce qui est important aujourd'hui dans les animations, ce sont les transitions. Qu'est-ce qu'une transition ? Une transition c'est une animation qui passe d'un état A à un état B.</p>

<p>C'est aussi ce qui sert à faire une pause, dans sa navigation et dans son parcours.</p>

<p>Johannes Tonollo, qui est un designer d'interaction a répertorié les différents types de transition qui existent aujourd'hui, donc il a fait une très grande liste, c'est pas mal donc il a tout répertorié en 6 catégories. La première est l'orientation, la 2ème extension spatiale, la 3ème les actions contextuelles, la 4ème la focalisation, la 5ème le feedback qui est la 6ème le feedforward, donc je vais vous les expliquer.</p>

<p>L'orientation, c'est la manière la plus logique dont on peut passer d'un état A à un état B. Donc par exemple, si on lit les différents articles d'un magazine, on lit, la manière la plus logique de passer entre les différents articles, c'est de tourner les pages, comme le fait le (feedboard ?).</p>

<p>L'extension spatiale, c'est ce qui consiste à réduire la complexité d'une interface en étendant son espace virtuel.</p>

<p>Les actions contextuelles, ce sont les actions qui sont liées au contexte. Donc par exemple, là, si on swape vers la gauche du testing, on a le bouton supprimer qui apparaît, donc c'est lié au contexte. La focalisation, c'est comment on concentre l'utilisateur sur l'action principale. Par exemple, là, sur Google drive, si on clique sur créer un document, on floute tout l'arrière pour se concentrer uniquement sur la (inaudible) et les actions qui sont à l'intérieur.</p>

<p>Le feedback, donc c'est le retour immédiat par rapport à l'action d'un utilisateur, comme par exemple le pull-to-refresh.</p>

<p>Le feedforward, c'est ce qui consiste à montrer à un utilisateur qu'il a des actions possibles comme par exemple sur l'écran d'accueil de l'iPhone où il y a le déverrouiller qui se met en sur brillances pour montrer que l'utilisateur peut déverrouiller son iPhone.</p>

<p>Alors quand est-ce qu'on utilise les animations ? Donc pour ça, il faut se poser différentes bonnes questions, dont la première consiste à se poser des questions sur l'attention de l'utilisateur.</p>

<p>Donc finalement quand on crée une animation, quand on veut la mettre sur son site, où est-ce qu'on veut que l'utilisateur regarde ? Est-ce qu'on veut qu'il regarde l'animation, est-ce qu'on veut qu'on regarde, que l'utilisateur regarde en haut à gauche pendant que l'animation se joue en bas à droite ? Donc voilà.</p>

<p>Le 2ème type de questions qu'on peut se poser concerne le but de l'animation. Est-ce qu'on veut que l'utilisateur interagisse sur l'animation, est-ce que c'est une transition, ou est-ce que c'est une animation entre 2 actions ?</p>

<p>Le 3ème même type de questions qu'on peut se poser, c'est la fréquence des animations. Donc combien d'animations va voir notre utilisateur dans son parcours ? On ne demande pas de compter, mais plus de nous jouer au niveau du ressenti et de faire le parcours vraiment du début jusqu'à la fin pour voir si c'est pas trop ou pas assez.</p>

<p>Le 4ème type de questions qu'on peut se poser concerne la mécanique de l'animation. Donc est-ce que les animations sont déclenchées par l'action d'un utilisateur ou indirectement, par exemple quand il scrolle, ou je ne sais pas.</p>

<p>Donc finalement, après que l'on se soit posé ces questions, on sait si on doit mettre des animations ou pas dans son interface. Parce que finalement, si on en met trop, enfin pour moi, mais mieux vaut ne pas en mettre assez que trop, parce que trop c'est vraiment on perd vraiment l'utilisateur, mais finalement si on n'en met pas assez, on peut le perdre aussi parce qu'il arrivera pas à contextualiser son environnement. Mais forcément il va vivre une meilleure expérience derrière.</p>

<p>Quelques bonnes pratiques. Pour la première je m'adresse aux développeurs, donc messieurs les développeurs, mesdames, testez votre code, en dehors de tester votre code, testez si l'animation lag ou pas sur les différentes machines.</p>

<p>Pour la 2ème bonne pratique, je m'adresse aux UX ou aux Créa, testez, alors testez, je demande pas juste encore une fois de vérifier que l'animation fonctionne, mais il faut tester dans la vraie vie, parce que finalement un utilisateur, il va pas forcément être chez lui devant son ordi ou avec sa connexion Internet à 500 000 Gigas, mais il sera peut-être dans le métro, au supermarché, dans un endroit où il aura pas forcément une très bonne connexion, et c'est là où il faut voir comment ça fonctionne et ce que ça fait vraiment, si ça fait pas une cassure dans tout le parcours.</p>

<p>Donc finalement, nous UX ou Créa qui concevrons les animations, nous sommes normalement garants de la bonne expérience d'un utilisateur derrière, donc on conçoit les animations, on les donne aux développeurs qui vont les développer et qui vont peut-être pas forcément vérifier derrière, comme je le disais, si ça va fonctionner vraiment ou pas, alors que le code marche, mais est-ce que devant ça marche, on sait pas et donc il va se dire que le chef de projet va vérifier de toute façon son travail derrière. Et le chef de projet il dit, bah de toute façon il y a des UX qui sont là pour garantir la bonne expérience des utilisateurs, donc c'est eux qui vont vérifier si tout fonctionne bien au final et donc tout le monde se passe de mot et personne ne vérifie à la fin si ça fonctionne ou pas.</p>

<p>Donc là, ce qu'il faut retenir, c'est que finalement ce sont des détails, mais tous les membres d'un projet sont garants du fait qu'un projet se passe bien et notamment là-dessus parce que ce sont des petits détails et on ne sait jamais vraiment qui les fait, qui va vérifier derrière tout ça.</p>

<p>La 3ème bonne pratique c'est Focus on the basics, c'est en gros, il existe des standards de l'animation donc comme je vous l'ai dit tout à l'heure, un designer d'interaction les a catégorisés, donc c'est pas mal, on peut s'en servir, il y a une grande liste et plein d'exemples. Donc, ne réinventez pas la roue. Mais si jamais vous osez le faire, parce que ça peut arriver, il faut tester pour voir si ça fonctionne ou pas, comment les utilisateurs réagissent et ne pas se lancer comme ça sans savoir si ça fonctionne ou pas.</p>

